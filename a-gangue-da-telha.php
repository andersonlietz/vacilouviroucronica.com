<p>No Uber o homem pensava em como iria aproveitar sua folga. Planejava chegar em casa, abrir uma garrafa de vinho ou uma latinha de cerveja,
   talvez coar café. Estava farto de viajar, e o lugar em que mais desejava passar alguns dias era em casa. 
   Não havia praia no mundo que ele deseja-se agora mais do que passar uma tarde na poltrona da sala,
    lendo alguns livros que havia comprado durante suas viagens e reservou para apreciá-los em casa, sem pressa e sozinho.</p>

<p>Ao chegar no destino o carro parou, o motorista tirou as malas com agilidade e colocou na calçada, 
  enquanto o homem se espreguiçava. O Uber se despediu e foi embora, ele pegou duas malas e atravessou a rua.
   Ao abrir o portão e buscar as outras malas, parou um momento e ficou observando a cunheira da casa.</p>

<p>— Cadê o meu telhado? — exclamou, enquanto soltou as malas no chão e se afastou para observar melhor.
   Não era impressão, as telhas haviam sumido, podia ver o ripamento do telhado e o foro. Mas o portão estava
    cadeado e o muro era razoavelmente alto, as portas da casa estavam intactas e nada dentro dela sumiu.
     Duas perguntas lhe tiravam a paz:</p>

<p>— Por que alguém roubaria telhas se era mais fácil pegar o dinheiro e as coisas que estavam dentro da casa?
   Como iria explicar isso para a Polícia?</p>
   
   <p> — Chamou as autoridades e registrou o Boletim de ocorrência, 
    mas os policiais não levaram muito a sério a investigação. 
    Passou a folga comprando mais telhas e ajudando os homens que contratou para cobrir a casa novamente.</p>

<p>Mas logo a polícia teve de atender outro caso parecido na cidade, e em um lugar após outro começaram a sumir telhados. 
  Nas cidades vizinhas o fenômeno também passou a acontecer. Percebeu-se que se tratava de uma quadrilha, 
  pois em tão pouco tempo e sem despertar a atenção dos donos das casas e de vizinhos, 
  a quadrilha destelhava uma casa em poucas horas e até em alguns minutos. </p>

<p>Até que um dia a casa caiu, a polícia interceptou um caminhão com telhas roubadas de uma casa,
   e os detetives entraram em ação. Prenderam o chefe da quadrilha após interrogar os meliantes no caminhão.
    Eles tinham ficha limpa e não sabiam estar cometendo um crime, eram trabalhadores honestos que destelhavam as 
    casas como lhes era pedido, carregavam o caminhão e as levavam até uma fazenda, de forma que os donos das casas 
    pudessem por telhas novas. Acreditavam trabalhar em uma empresa de destelhamento.</p>

<p>Ao interrogar o chefe da quadrilha, descobriram o destino das telhas, na fazenda e sem a ajuda dos empregados,
   o meliante triturava e peneirava o pó da argila em busca de algo precioso, e depois de peneirado o pó da argila
    era vendida para uma olaria e usado para fazer novos artefatos de cerâmica. A polícia não conseguiu tirar do meliante
     a motivação do crime, ele não revelou o que procurava no pó da argila.</p>

<p>Anos depois, em uma cidade vizinha, um grupo de crianças brincando em um terreno baldio encontrou, em meio a um monte de entulho,
   uma pedra transparente e que brilhava, brilhava digamos, igual diamante.</p>

