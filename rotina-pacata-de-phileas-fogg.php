         <p>O primeiro dia do ano foi marcado por um calor escaldante ao meio dia.
            Depois de almoçar na casa da minha mãe, voltei para a minha,
             enfrentando os raios ultraviolentos atirados sem trégua pelo sol. 
             A cama me estendia  uma proposta irrecusável, e o calor não me permitiu fazer outra coisa a não ser tirar a sesta. 
             Deitei e logo adormeci. Acordei depois de um tempo, conferi o horário no celular, 
             me virei na cama e voltei ao que estava fazendo antes.</p>

<p>Quando acordei novamente, revigorado pelo descanso, 
  coei uma xícara de café e me sentei na poltrona da cozinha com o meu exemplar do livro:
   “Volta ao mundo em 80 dias”. O marcador estava em um dos  capítulos iniciais,
    e depois de o retirar, continuei a leitura sobre a vida “simples” em que vivia o Mr. Fogg antes de sua famosa viagem. </p>

<p>Gentleman, dono de uma fortuna de origem misteriosa, aproveitava seus ativos
  de forma moderada. Apesar de possuir uma casa confortável, tirava proveito dela apenas a noite, 
  pois durante o dia frequentava um clube renomado de cavalheiros, lendo jornais, jogando “whist” com os amigos e tomando as refeições.</p>

<p>O autor prendeu a minha atenção desde o início,
   pois essas descrições de rotinas calmas e pacíficas costumam ser do meu interesse. Leituras, chás, 
   conversas com bons amigos, situações também abordadas em livros de  Jane Austen, Agatha Christie e outros. </p>

<p>Interessante pensar que esses hábitos reclusos no livro eram de um homem abastado, 
  pois acredito que uma das maiores ambições humanas é ter dinheiro e liberdade para viajar pelo mundo.
   Não pude ler sem admirar a vida do Gentleman e comparar  os pontos que a minha tem em comum.</p>

<p>Em Fevereiro de dois mil e vinte, assim como para muitas outras  pessoas, ocorreu o início da minha reclusão. 
  Daqui um tempo fará dois anos que não saio da minha pequena cidade. Moro em um sítio, razoavelmente perto de uma vila,
   também chamada carinhosamente de “Colônia” pelos mais velhos.
    O mais perto que já cheguei de sair do território dela nesse período foi quando acompanhei o 
    meu tio para comprar pêssegos na rodovia. </p>

<p>A rotina calma descrita por Júlio Verne me lembrou das  poucas vezes que vou para a vila fazer  compras, 
  e das  poucas visitas que fiz  aos meus amigos. Apesar de gostar de viagens, não foi tão difícil me acostumar,
   aproveitei para exercitar o meu lado  caseiro. Estudei, escrevi, cuidei dos animais e das manutenções do sítio,
    criei este site e passei a publicar semanalmente, então essa vida reclusa foi razoavelmente proveitosa.
Assim como Mr. Fogg, vou me preparando, estudando e administrando os meus ativos,
   e quando o vento da aventura soprar, talvez ir há Gramado, Chile ou Baviera.
  </p>


