<p>Se tem algo que acontece em praticamente todas as escolas, são aquelas situações de Bullying dos machões. Aqueles caras candidatos a bandidos em pleno ambiente educacional. Como estão em uma posição aparentemente privilegiada, mais altos e fortes, se vêem na obrigação de ensinar os outros menos privilegiados de como a vida é dura. Nem precisam bater, em vários casos já basta amedrontar com ameaças os coleguinhas.</p>

<p>Em minha escola havia dois garotos que eram temidos por todos os demais alunos, sempre com seus clãs, não lembro se eram do mesmo grupo ou de “gangues” diferentes.
Faziam as brincadeiras que queriam com quem queriam, sendo que acima deles só havia a autoridade dos professores e da direção escolar, mas geralmente escolhiam desobedecer.</p>

<p>Um dia ocorreu o espetáculo, não era circo novo na cidade nem rodeio, apenas uma discórdia entre os dois tiranos. Nunca descobri o motivo, ou não me lembro, mas devia ser algo bem bobo, acredito que não havia nenhuma menina envolvida na richa. É claro que isso representava alguma esperança de alívio a todos os que eram alvos de brincadeiras de mal gosto e provocações, como escravos que veem uma esperança de liberdade na morte de seu senhor.</p>

<p>Estávamos ansiosos, pensávamos que iríamos ver algo como a fala do professor de português quando ia entregar nossas provas e revelar nossas notas: “dois caminhões de combustível em alta velocidade colidindo de frente”. Uma luta entre os dois garotos mais violentos e intransigentes da escola seria algo espetacular. Já imaginávamos a imprensa, os camburões da polícia e talvez o IML.</p>

<p>Porém a “demonstração de força” foi um verdadeiro fracasso. De alguns gritos a discussão foi se tornando
     um festival de tapas, alguns mais fortes e outros mais duvidosos, 
     até que em algum ponto desse estapeamento um conseguiu derrubar o outro na grama do pátio e subiu por cima dele.
      Agora ele estava em uma situação delicada, já estava ganhando a briga e estava sentado em cima do oponente,
       com todos os alunos assistindo e algumas pessoas da diretoria da escola já estavam indo por ordem na bagunça.
        Mas ele era machão, não podia “arregar” com todos em volta olhando. Então com a mão aberta deu alguns tapas 
        com a palma no adversário. Defini um nome pra isso como: “Socotapa”,
         um soco bem tímido. No final os adultos salvaram eles de passar ainda mais vergonha, 
         os levando para a diretoria para dar uma palavrinha. Se isso não acontecesse nós ouviríamos uma
          frase igual ás proferidas no coliseu romano:</p>
<p>— “Alguém entregue canivetes a eles!”</p>

