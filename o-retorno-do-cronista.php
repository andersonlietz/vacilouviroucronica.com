<section id="content">
<p>Cá estou, sentado com o computador na mesa da cozinha, escrevendo mais uma crônica. Não escrevo uma crônica comum, é a crônica de retorno, de abraço forte, tapinha nas costas e carinho nos cabelos. Um retorno que não seria preciso, pois eu não fui a lugar nenhum, apenas deixei por um momento (quase dois meses), de expor meus devaneios e lembranças neste cantinho da internet. Talvez depois desse tempo eu seja apenas um desconhecido para você, um mero mortal escrevendo palavras vazias na areia da praia, ou rascunhando o vento. Mas se sentiu minha falta, sinta-se abraçado, e saiba que voltei aqui por você.</p>

<p>Mas afinal, por que eu deixei de escrever? Devo confessar, é uma boa pergunta. Tudo começou quando passei a me dedicar escrevendo mais do que uma crônica por semana, a fim de ter crônicas exclusivas e talvez no futuro publicá-las em um livro. Passei a me sentir mal pois acreditava ter tantas outras coisas importantes a serem feitas e eu estava lá, escrevendo.</p>

 <p>Então em uma sexta-feira decidi que precisava escrever uma nova crônica a fim de publicá-la, e a um tempo permanecia em minha mente uma ideia de enredo, e comecei a dar os nomes e escrever as cenas. Criei a página e publiquei no servidor, mas não indexei na página inicial do site. Mandei o link a um amigo que avalia as crônicas e recebi seus elogios. Mas percebi que a história estava incompleta, eu não podia deixar de lado o restante, pois a ideia era ainda mais incrível.</p>

<p>Não queria postar um conto pela metade, e já estava no dia da publicação, o que me deixou ansioso. Eu não queria falhar, mas estava falhando. A semana passou e eu não publiquei nada. Outra semana veio, eu tinha esperança de então escrever duas crônicas, a daquela semana e outra para recuperar a lacuna da semana anterior. Mas não me ocorreu ideia, ou eu não quis aprimorar as que me apareceram.</p> 

<p>No fundo me sentia cada vez mais ansioso, pois meu motivo de orgulho era postar crônicas toda semana, ainda mais faltando poucos meses para concluir um ano de crônicas semanais. E passava semana após semana e eu adiava em cada uma delas o meu retorno. Não foram muitos os que sentiram minha falta, ou pelo menos, que comunicaram isso. Mas houve quem percebeu o meu sumiço e me incentivou a voltar a escrever.</p>

 <p>E como disse Franz Kafka: “ De um certo ponto adiante não há mais retorno. Esse é o ponto que deve ser alcançado”. Sinto que alcancei esse ponto, pois, por mais que escrever crônicas exija meu tempo e esforços, me faz ter certa medida de realização, por repartir alguns acontecimentos e ideias com outros e me dar uma sensação de produtividade, por registrar e materializar essas ideias.</p>

<p>E no fundo continuo sem assunto, mas como geralmente escrevo sobre escrever crônicas quando não tenho outra coisa a escrever, aqui está uma crônica sobre a escrita de crônicas, ou melhor, sobre o meu retorno a esse assunto, pois decidi dar uma explicação e iniciar uma nova era, provavelmente de crônicas melhores. E agora que quebrei o gelo, espero publicar novamente toda semana. E posso garantir uma coisa:</p>

<p>Como é bom estar de volta!</p>

