    <div class="espaco"></div>

    <p>Uma das coisas que sempre ouvi minha avó se vangloriar, é de que seu falecido pai, 
      meu bisavô, era um carpinteiro de mão cheia. Construía rodas d’água, moinhos, casas e
       vários outros artefatos de madeira. Em uma situação, fazendo rodas de carroça,
        um conhecido afirmou que ele não devia caprichar tanto, escolher a melhor madeira
         e fazer do melhor jeito que ele sabia, pois dessa forma a roda nunca iria estragar 
         e ele não teria o trabalho novamente.
Meu bisavô não seguiu esse conselho, e além de ter ganhado a confiança dos seus clientes,
 nunca se arrependeu de ter trabalhado da melhor forma que sabia.</p>

<p>Eu lembro disso quando sou obrigado a comprar bugigangas, quando uma bomba de encher pneu,
   depois de muitos remendos não pode mais ser usada e eu preciso ir em uma loja em que há umas três qualidades,
    todas do mesmo modelo que já comprei e sei que vai soltar uma peça nas primeiras vezes que eu usar.
     Comprar bomba de encher pneu é enxugar gelo. Nem dá mais para se vangloriar com esses bens materiais, 
     por exemplo se exibir assim para o vizinho: “Olhe vizinho, comprei uma bomba nova!”, pois todo mundo
      sabe que ela durará menos de um mês.</p>

<p>Guarda chuva já é algo descartável, não dá mais nem para reagir a um assalto com ele, o bandido cairá
   nas gargalhadas e poderá ser fatal. Usar como bengala, jamais. É uma das coisas que estragam até mesmo 
   encostadas na parede. Você chega em casa um dia com ele, encosta na parede e no outro quando pega para usar, 
   já têm uma varinha de alumínio  torta ou quebrada, ou o tecido solto. É inacreditável que alguém chegue ao ponto 
   de roubar guarda chuvas, talvez sejam questões de sobrevivência, pois o objeto em si não vale nada, pelo menos para 
   quem compra, não para quem vende. E o meu maior desgosto é saber que os chineses estão felizes da vida com seus guarda chuvas de bambu.</p>

<p>Esses dias descobri que as lâmpadas não queimam por necessidade, e sim por conspiração.
  Tudo em nome da economia, competições entre países e empresários, deixaram de fazer o mais durável
   para fazer o mais rentável. Isso é praticamente o que diz a minha “teoria do sapato”.
    Sim, acredito que o ser humano está se especializando em fazer espinhos mais resistentes e calçados cada vez piores.
     É uma  frase bem boba, mas acho que você entendeu a ideia. É outra forma de dizer que essa falta de capricho
      industrial é um tiro no pé (ou um espinho no pé).</p>

<p>Sei que é “pelo bem da economia”, mas me parece um atraso fazer um produto ou serviço ruim para poder fazê-lo
   novamente em breve. Mas sei que esta crônica não irá mudar tanto as coisas, as lâmpadas continuarão queimando,
    então me resta agradecer a você, que faz o seu melhor!</p>

