    <p>Não lembro de ter presenciado tão persistentes estiagens. 
         A do ano passado surpreendeu até mesmo os idosos que nasceram aqui. 
         Os imponentes córregos  escassearam sua água. As plantas perderam vigor, a grama estava morrendo.
          Tanto ano retrasado como no passado, as secas foram motivo de clamor por chuva.
           O milho plantado ali do outro lado da estrada sofreu, mesmo assim, produziu alguns milhões. </p>

<p>Mas a umas semanas atrás, as tão esperadas chuvas chegaram. Vieram tímidas, em forma de garoa, 
    que mal transformavam o pó em matéria prima humana. Até que elas persistiram, engrossaram, 
    até descambar de vez, raios e trovões nos faziam pensar se pedir por chuva havia sido uma boa ideia.
     Diferente de outras tempestades que aqui passaram, essas não causaram estragos, eram assustadoras,
      porém breves, uma ou outra apenas se prolongou. Os campos foram regados e as sementes nasceram.
       Aqui no sítio conseguimos semear uréia em nosso próprio milharal que carecia de água e mais nutrientes.</p>

<p>O agricultor aqui da frente colheu os pés de milho verde ( mesmo com as chuvas,
     o milho seco não daria o lucro desejado), e fez  silagem para o gado,
      já na mesma semana plantou a soja, que deveria produzir antes do inverno chegar.
       Apesar de conseguir espantar as pombas para não comerem as sementes, 
       foi surpreendido por nova seca, que fez  as sementes vingarem preguiçosas.
        O recurso foi passar a grade desconcertando as sementes de soja,
         e depois plantar feijão, uma nova chuva regou a terra e os brotos estão crescendo vigorosos.</p>

<p>E adivinhem! Sim, já estamos aqui desejosos por novas chuvas,
     que se recusam novamente a banhar a terra. Os dias realmente estão bonitos,
      o sol é uma dádiva que torna as cores mais radiantes, os pássaros cantam alegres,
       as crianças brincam felizes ao ar livre. Já os adultos  preferem a sombra e o vento fresco.
        Se refugiam em baixo de varandas e árvores. Porém há um temor de que a seca persista,
         e como tudo em excesso faz mal, o solo seca, a poeira invade as casas, as plantas definham, 
         os córregos secam. Pelo menos há um assunto que todo mundo pode puxar sem medo com os desconhecidos:
          <br>“Será que chove?”.<br> E o outro responde: <br>“Que vai chover vai! Só não sei quando!”</p>


