      impossibilitando o surgimento de novas. Minha criatividade é assim, tenho algumas ideias e elas ficam solitárias 
      por um tempo em minha cabeça, enquanto não as uso, novas não aparecem.</p>


<p>Mas quando tenho ânimo e escrevo mais, elas surgem cada vez mais, das mais variadas formas,
   porque aparecem e logo são usadas, liberando espaço para que uma nova apareça.
    E é o que algumas pessoas me perguntam: “De onde vêm essa criatividade?” E eu não sei como responder, 
    pois virou um processo natural, apenas busco cada vez mais não perder as ideias que surgem.
     Ao contrário de quando escrevi minhas primeiras crônicas, quando várias ideias que passavam pela 
     minha mente eram logo descartadas, e quando eu julgava uma boa, acabava estragando na hora de escrever.
      E fico pensando quantas crônicas teria escrito se aproveitasse todas as ideias sem exitar. 
      Mas não posso lamentar, a criatividade é infinita e as ideias vão se renovando. 
      Então vou escrevendo, talvez de alguma forma me ocorra novamente essa ideia,
       e eu publique finalmente, a crônica esquecida.</p>

