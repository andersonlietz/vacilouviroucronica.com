<p>Começou a época dos pinhões.  Não oficialmente, acho que a venda está proibida, mas eles já  estão caindo e o consumo acontecendo.
     Os pernilongos estão fazendo a festa, esses pequenos vampiros existem, podem voar de dia e procuram suas vítimas,
      os catadores de pinhão. Logo logo chegam as geadas para amenizar esse nosso sofrimento, os pernilongos deixarão de existir, 
      pelo menos até o inverno acabar. </p>

<p>Esses dias em uma tarde fui pegar umas frutas na casa da minha mãe e ela  me mostrou uma panela em cima do fogão,
     havia colocado os pinhões cozinhar e agora estavam fazendo cheiro ao meu nariz. Ela já havia comido alguns,
      então me ofereceu e eu prontamente sentei ao lado do fogão e comecei a me servir. Tábua de cortar carne no colo,
       uma concha na panela para pegar os pinhões, e um utensílio de madeira como martelo para abrir a casca e retirar a
        massa comestível de dentro.  </p>


<p>Minha avó estava como de costume sentada em sua poltrona ao lado do fogão, então fiquei com vergonha de comer sozinho,
     comecei descascar um pinhão pra ela e dois pra mim. Sua memória lhe fez recordar de algumas coisas que ela geralmente
      conta nessa época, e nós da família ouvimos em todos os invernos. Que ela e uma de suas irmãs mais velhas iam juntar 
      pinhões em um pinheiro gigante perto da casa da família quando elas eram novas, pinheiro este, acredito eu, era o mesmo 
      que foi derrubado para madeira quando o pai delas vendeu as terras. Mas dessa vez ela se concentrou nas recordações mais alegres.  </p>


<p>— “Têm que ver se aquele pinheiro ali em cima perto das canas deu pinhão esse ano! Eu sempre juntei bastante ali.” — 
    falou ela, recordando novamente de quando ainda conseguia com seus passos lentos ir nos pinheiros mais
     próximos da casa ajuntar os preciosos pinhões.
— “A maioria desses que eu ajuntei foi ali!” — a minha mãe respondeu, dando um certo alívio para a minha avó,
 lhe mostrando que os pinhões não estão “se perdendo”. </p>


<p>Naquele mesmo pinheiro foi tirada uma das fotos mais fofas da infância minha  e do meu irmão. 
     Eu devia ter entre quatro e cinco anos, meu irmão, entre doze ou treze. E claro, estávamos juntando pinhões,
      cada um com um baldinho, nossa mãe fez questão de registrar aquele momento. Entre nós e não menos importante
       estava a faísca, uma cadelinha pequena que fez parte da minha infância e marcou várias histórias da família
        (eu a chamava de “Viquiti”, não me perguntem o porquê). </p>


<p>Já que estou falando de pinhões e família, lembrei de um jogo ao estilo Las Vegas, feito não com cartas e roletas,
     mas com, adivinhem: pinhões. E foi ensinado pelo meu pai, no calor aconchegante da cozinha quente devido ao fogão a lenha,
      mesmo em uma manhã de geada. Já não lembro direito as regras, mas consistia em descascar um pinhão, assado na chapa,
       quebrá-lo ao meio e deixar que o outro participante adivinha-se qual parte do pinhão estava na mão do adversário, 
       se a parte apontada ou a parte arredondada. Se acertasse comia o pinhão, era um jogo com prêmio saboroso e com
        cinquenta por cento de erro e acerto, mais generoso que os de Las Vegas. </p>


<p>Essas são algumas lembranças que surgem nos meus invernos, por isso não posso dizer que odeio o inverno e amo o verão,
     até já escrevi uma crônica sobre isso. O inverno tem suas peculiaridades, o sol é mais precioso, a cama é mais gostosa,
      os pinhões aparecem embaixo dos pinheiros, o chimarrão ganha seu mérito e o lugar ao lado do fogão a lenha é o mais disputado.
       São as sensações do inverno, a época do Pinhão. </p>

