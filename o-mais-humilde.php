<p>Algumas pessoas estavam almoçando em um restaurante quando um homem se deparou com outro à sua frente o encarando com uma feição intimidadora.</p>
<p>— O que foi? — perguntou.</p>
<p>— Não adianta disfarçar, sei que você é um desses arrogantes que andam por aí!</p>
<p>— E porquê você acha isso?</p>
<p>— Você está comendo de garfo e faca, é o  auge da arrogância. Eu, como homem do povão que sou como apenas com o garfo.</p>
<p>— Então é assim? GARÇOM, traga uma colher aqui, vou mostrar pra ele quem é o humilde.</p>
<p>O garçom cumprindo seu trabalho, pegou uma colher na cozinha e voltou empolgado para saber o que reservava aquela discussão. Vendo que agora o outro estava comendo de colher, o que começou a intriga teve que apelar.</p>
<p>— GARÇOM! Pode levar esse garfo — falou enquanto erguia o queixo, abria a boca e comia o macarrão com as mãos.</p>
<p>— Olha aqui, eu como de boca aberta, escute os meus dentes mastigando a comida — disse o outro que havia largado a colher e estava comendo com a mão.</p>
<p>— Isso não é nada! —  disse com a boca cheia — escute o meu arroto.</p>
<p>E a exibição de “humildade” continuava.</p>
<p> — Olha como eu estou descabelado!</p>
<p>— Eu estou sem sapatos — disse um entregando os sapatos para um vizinho de mesa. </p>
<p>— E eu estou descalço — falou o outro dando o sapato e as meias embora.</p>
<p>— Estou sem a gravata, você é um engravatado orgulhoso! </p>
<p>O outro também tirou a gravata, e logo começou a desabotoar a camisa.</p>
<p>Em outras circunstâncias os dois seriam convidados a se retirarem do restaurante, mas a discussão estava tão surreal que entretia os clientes e ainda atraía novos que passavam na rua e entravam para ver o que estava acontecendo.</p>
<p>Logo os dois estavam sem camisa. </p>
<p>— Olha aqui, tenho pelos no peito e uma barriga de chop! Legítimo homem do povo! </p>
<p>O outro magro e depilado se viu obrigado a tirar as calças: — Olha esses rasgos na minha cueca! </p>
