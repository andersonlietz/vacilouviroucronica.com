                      <h2>Briques Escolares</h2>
	
	<img src="imagens/gude.jpg" class="imagem"></img>
    <!---->
    <div class="espaco"></div>
    <p>Provavelmente você já presenciou  aqueles negócios na escola, 
        também chamados de “briques”, (ou escambo, se você estudou durante a idade média).
Eu não gostava de fazer estes negócios, sempre fui muito apegado com as minhas coisas, mas um dos briques vou relatar aqui. </p>

<p>Em um momento troquei algo por um fusca em miniatura da Hot Wheels.
     Valeu a pena, pois nem lembro do que me desfiz para conseguir ele.
      A minha impressão é que ele era azul escuro ou roxo. 
      Em uma das brincadeiras aconteceu um imprevisto, uma das rodas se soltou, e como não veio chave de roda,
       macaco ou estepe, recorri ao zelador da escola.
       Eu e meus amigos o ajudávamos algumas vezes durante o recreio, 
        então ele prontamente aceitou a tarefa e consertou a roda usando um alicate. </p>
       
<p>A minha alegria durou pouco. Um tempo depois, talvez no mesmo dia,
    impelido por aquela adrenalina  que move  Las Vegas e Wall Street,
     fiz um acordo com um colega de classe: ele ficaria com o meu fusca, de uma marca famosa e feito de metal, 
     em troca de um Ioiô,  pedaço de plástico ressecado pendurado em um barbante e com procedência duvidosa.
      Mais tarde ele completaria com um real para ficar mais "justo". Vale ressaltar que “naquela época o dinheiro tinha valor”. </p>

<p>Acordo feito, me despedi do fusquinha, e comecei a tentar fazer aquela coisa subir o barbante e 
    encaixar na minha mão, técnica  que não sei até hoje.
     Acreditando naquela filosofia de que tudo pode ser conquistado pela força, 
     o arremessei com vontade  para baixo,
      mas calculei errado o comprimento do barbante em relação ao piso, e logo vi as duas partes rolando uma para cada lado. 
      Dessa vez o zelador não pôde me ajudar, mas guardei o brinquedo, esperando um dia em que inventassem uma cola mais potente.
       e hoje quando fui procurar para fotografar e ilustrar essa crônica, não o achei em lugar nenhum, 
       provavelmente alguém que desconhecia o meu projeto de restauração o atirou ao lixo. </p>
<p>Não tinha como desfazer o acordo, foi imprudência minha que causou aquela fratura.
     Então como consolo, aguardava a minha moeda com ansiedade, mas pelo que eu lembro,
      não a recebi.</p>

<p>Mas como quem Vacila vira Crônica (risos malévolos),
     lembrei de um recreio épico na escola. Eu e meus amigos bolinhas de gude no pátio,
      em um lugar onde haviam sido enterradas fossas a grama ainda não havia crescido. O irmão mais velho do 
      garoto que me devia a moeda veio acompanhado de outro garoto, e do nada deu um “chá de cueca” em
       um colega que estava de pé nos assistindo tranquilo com seu prato de comida. </p>

      <p>
     Caso você não saiba o que é esse “chá de cueca” ao qual eu me refiro,
     o marmanjo pegou o elástico da calça do meu colega e ergueu.
      Nem preciso dizer que é muito desconfortável (sim, já fizeram comigo).</p>
      <p> Pelo que eu me lembro, aquele foi um caso peculiar, o menino suspendeu o meu colega pela calça,
        que se desequilibrou com o prato de comida e foi com o peito para a frente,
         derramando o lanche em nossa “arena” de bolinhas de gude. Os pais dos envolvidos foram chamados, e
          talvez tenha dado algum processo judicial. Ouvi que a avó do meu colega exigiu uma cueca nova,
           sim, segundo o boato, a outra rasgou durante o chá. </p>

<p>O pedaço de carne perdido com o incidente estava atrapalhando o nosso jogo, 
    então alguém (esse “alguém” talvez até tenha sido eu), o chutou para longe.
    </p>

<p>Uma nota ao meu colega que foi o alvo do chá: 
    lembro de você rindo conosco do ocorrido, então acredito que não seja errado escrever sobre isso.
      Talvez você leia essa crônica e ria novamente. </p>

 

