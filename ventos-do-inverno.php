<p>O dia foi frio, um sinal claro do outono e mostra que o inverno está chegando. 
    Depois de fazer alguns trabalhos dentro de casa, decidi ir caminhar no campo e juntar pinhões.
     Peguei uma jaqueta de pano e outra de couro, o chapéu de palha, as botas e o balde e fui.
      Assoviei para que as cadelas me acompanhassem, mas das duas, apenas uma apareceu. Havia poucos pinhões,
       e o vento era frio, misterioso. Não sei o que me agrada no frio, mas tenho uma sensação de contemplação com ele.
        Gosto de paisagens com cores frias, neve, pedras, rios, e me agradei das passagens registradas em livros
         como “O Castelo”, de Kafka, “Crime e Castigo” de Dostoievsky, e os contos de Tchekhov, paisagens e situações 
         deprimentes e frias.</p>

<p>Andar pela floresta com esse vento frio me faz lembrar, assim como outras coisas, dos meus antepassados e
     povos antigos. Li que os índios da minha região costumavam ir para o litoral quando o inverno chegava, 
     por ter temperaturas mais agradáveis nessa época. Costumavam, foi um dos costumes perdidos com a colonização
      da região. O primeiro morador europeu do município, Pedro Ermonge, conseguiu conviver por um tempo com os índios,
       mas depois de uma discórdia ele e seus dois sobrinhos foram ameaçados e mortos, com flechas indígenas. 
       Na escola e em livros é apontado que os índios foram “expulsos”, para um local que hoje é a reserva do Quati.
        Isso ocorreu quando um grupo de colonizadores europeus chegaram, mas nunca ouvi falar se houve uma batalha 
        ou os índios decidiram se retirar para evitar conflitos.</p>

<p>Meu tataravô subiu a serra com a família depois que veio da Alemanha, e abriu a primeira picada para essa 
    direção da pequena vila. Construiu um abrigo provisório embaixo de um grande tronco de árvore, enquanto
     seus maiores esforços se concentravam nas lavouras. O frio me faz lembrar disso, dos índios com suas
      técnicas para vencer o frio e a família do meu tataravô descansando e se abrigando das geadas embaixo 
      daquele tronco de madeira. Mas meu tataravô Josef não teve tanto sossego, a minha tataravó Apolônia repetidas 
      vezes viajava para visitar a família dos seus pais em Corupá, região mais litorânea e provavelmente menos fria.
       Acredito que ela tinha uma saúde frágil e por isso o Tata Josef permitia que ela ficasse tanto tempo longe.
        Não os conheci, não tenho nenhuma foto deles, mas a impressão que tenho dela é de que era uma princesa.</p>

<p>Depois de juntar alguns pinhões, comecei a seguir o caminho para casa. Na estrada de grama uma lebre apareceu 
    dando seus pulinhos, ao me ver pulou novamente para o capim e se escondeu. Mais adiante em uma baixada, 
    ao observar as árvores de Pínus que eu podei a algum tempo atrás, me deparei com pinhas. Essas árvores 
    são pelo que eu me lembre, de regiões frias, e suas pinhas são ótimas para fazer fogo e aquecer os invernos. 
    É para mim um sinal de esperança, pois lembrei das árvores de Pinus plantadas pelo meu avô e meu pai, 
    provavelmente vieram delas as sementes que germinaram e nasceram aqui. Lembro das vezes em que fui juntar 
    pinhas com meu pai, embaixo das suas árvores e embaixo das que o meu avô plantou. Nenhuma daquelas árvores restou, 
    mas fico feliz que existem novas produzindo suas pinhas. Essas são algumas das sensações que aparecem em meus
     invernos.</p>

