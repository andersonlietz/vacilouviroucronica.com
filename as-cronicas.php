<p>Eu me encantei pelas crônicas a um tempo atrás, provavelmente você já percebeu. 
  Para mim, a crônica passou a ser uma literatura singela, sublime, delicada.
   Mas é claro, talvez as minhas não consigam exprimir essas qualidades aos leitores, tenho plena consciência de que sou um aprendiz, (e, exaltando minha modéstia), nem aos pés dos mestres do gênero.</p>

 <p>Comecei minha jornada de escrita criando o início de algumas estórias que, ao meu ver, se tornariam livros razoavelmente grandes, com vários capítulos instigantes. Mas ficava perdido em minha própria criatividade, desenvolvia o início e via meios demais para chegar à conclusão. Daí conheci o conto, que se caracteriza por ser uma estória curta e com poucos personagens, até fiz um curso que me ajudou a organizar os enredos e dar mais vida a eles. O tempo que levo para escrevê-los ainda é grande, acabo desanimando por alguns meses e depois de um tempo, retomo o projeto.</p>

<p>A descoberta da crônica e dos cronistas foi um grande marco na minha vida. Porque por mais que ela seja encarada com pouca importância frente aos outros gêneros, com certeza mudou muitas vidas e foi muito bem usada por escritores de renome. É incrível, por exemplo, saber alguns acontecimentos na vida desses escritores, seus bastidores, suas divagações, gostos ou sentimentos. Também há o contexto histórico, em que as crônicas de vários autores foram retratando o Brasil e suas mudanças, tanto estéticas, bem como as de cultura e valores.</p>

<p>Com essas grandes obras primas da crônica, escritas por autores conhecidos, me ocorre a “Síndrome do Impostor”, me ocorrem perguntas como: “Será que o que eu escrevo realmente são crônicas?”, “Posso ser chamado de cronista? “, e “Será que meus textos são prejudiciais para o reconhecimento da literatura?” Fiquei muito lisonjeado com o que um amigo disse sobre as minhas crônicas: “Ao meu ver, o que você escreve é literatura!”. E outro: “Você é cronista sim!”. Frases assim me incentivam muito, mostram que há pessoas que admiram o que eu escrevo e torcem por mim. Mas pelo bem dos meus textos não me convenço plenamente, preciso muito melhorar, me esforçar mais e escrever crônicas ainda melhores.</p> 


