<p>Alguns dias atrás tive um susto quando vi um comunicado de falecimento no facebook.
    
Não acreditei, havia algo de errado, pensei que era alguma pegadinha de primeiro de Abril, mesmo sendo início de Agosto. Fui digerindo a notícia e no outro dia de manhã descobri por fontes confiáveis de que ele realmente faleceu. Não éramos amigos, era uma relação de subsistência, ele precisava vender e eu precisava comprar, mas geralmente comprava por mero consumismo. </p>

<p>Ele tinha uma “Loja de Cacarecos”, que era famosa por seus itens duvidosos. Para quem sabia comprar podiam ser bem úteis, o azar era de quem escolhia o item errado, e confesso que era (sou) desse grupo menos afortunado. Havia em suas prateleiras: louças, bibelôs, livros, dicionários, materiais escolares, brinquedos, relógios, roupas, canivetes, bombinhas, bijuterias, cremes, e nos últimos anos, garrafas de vinho. A loja fica perto da escola, e algumas vezes me fez perder o ônibus que me levava da escola para casa, pois perdia tempo comprando algumas bobagens. Certa vez vi um bastão de madeira tipo um taco de beisebol, perto do balcão, e brincando perguntei:</p>

<p>— Esse taco de madeira você vende por quanto?</p>
<p>— Não está à venda, eu tenho ele para caso algum bêbado entre aqui e comece a bagunçar!</p>

<p>Outra vez eu olhava as prateleiras e escutei a conversa dele com um menino que foi devolver alguns CDs de jogos de computador. Se não me engano o menino se arrependeu da compra, talvez enjoou dos jogos ou não funcionaram no computador dele.</p>

<p>— O negócio é o seguinte, — disse o vendedor, apelidado como “Ede”, — eu posso ficar com eles e devolver o dinheiro, mas não vou poder te vender nada da minha loja outra vez.</p>

 <p>O menino aceitou o acordo com tranquilidade, e fiquei pensando como ele viveria sem poder extravasar seus desejos consumistas na loja do Ede. Eu mesmo comprei muitas porcarias na loja dele, mas estava lá sempre conferindo as novidades. Nos últimos anos acabei sendo conquistado pela concorrência, então passei a frequentar menos a sua loja. A última vez que comprei lá foi para comprar um guarda chuva, pois começou a chover e eu precisava de um para chegar em casa. Já havia comprado um lá uma vez, que virou pura decepção, e estava com medo de ser decepcionado novamente, mas a necessidade era maior. Escolhi um preto, de armação dupla, perguntei (talvez provocando): </p>
<p>— Será que esse guarda-chuva dura tempo?</p>
<p>— Dura! Pode durar uns 5 anos (ou quinze, não lembro bem), se você não deixar cair e nem amassar ele.</p>
<p>Estava com pouco dinheiro e pedi se podia marcar o restante do dinheiro para pagar outro dia.</p>
<p>— Pagar quando? — perguntou.</p>
<p>— Essa semana ainda — disse o que ele queria ouvir.</p>
<p>E por mais que ele me conhecesse e eu já tinha marcado coisas lá e pagado, ele pronunciou a seguinte frase novamente:</p>

<p>— Sabe que o preço dobra de valor se demorar mais de um mês para pagar? </p>

<p>Nem precisei responder, mas envergonhado, afirmei com a cabeça. Parecia estar negociando com um agiota, que cobra juros inescrupulosos e têm um porrete atrás do balcão. Fico pensando nessa última compra, sem saber, o vi pela última vez (eu paguei a dívida, mas sem vê-lo). Imaginei sua despedida:</p>
<p>— Lietz, é a última vez que nos vemos, obrigado por ter comprado os meus produtos, e desculpa pelos que estragaram. </p>
<p>— Mas como assim? Você vai embora? Voltar para a Suíça?</p>
<p>— Bom se fosse, mas não, vou, bem, você sabe, descansar para sempre.</p>
<p>— Ahh!</p>
<p>— Se cuida, tenha uma família, e aproveite esses anos!</p>
<p>Mas não houve essa despedida, ele simplesmente foi, sem imaginar. Vai ser difícil esquecer, pois em cada canto, há uma das suas quinquilharias.</p>

