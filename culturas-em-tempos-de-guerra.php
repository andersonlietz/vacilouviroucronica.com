                      <h2>Culturas em tempos de guerra</h2>
	
	<img src="imagens/culturasemtemposdeguerra.jpg" class="imagem"></img>
    <!---->
    <div class="espaco"></div>
    <p>Hoje eu estava como de costume tomando meu café da manhã com minha mãe e avó.
       O rádio estava ligado na estação “local”, e ouvi a jornalista citar o nome do meu ex-professor de Ciências Humanas. 
       Ele é bem engajado na divulgação e preservação da cultura ucraniana no Brasil,
        até mesmo promoveu algumas apresentações do grupo cultural de dança Ucraniana nos festivais da minha escola.
         Ele concedeu uma preciosa entrevista ao Jornal salientando a comoção em todo o mundo com a 
         invasão praticada pela Rússia, e que as tensões já ocorrem desde a independência do país.</p>

<p>Me senti orgulhoso de ter sido seu aluno e senti saudades das aulas com ele.
    Lembrei de um episódio curioso daquela época. Ele mantinha uma barba, simétrica e bem caprichada, 
    o que não o resguardava de receber  apelidos constrangedores. Uma vez veio para dar aula vestindo uma 
    dessas jaquetas volumosas, que podem estar vazias por baixo ou cobrindo um cinto de explosivos.
     E como eu não sabia a diferença entre Ucrânia  e Oriente Médio, cogitava a possibilidade de, em alguns instantes,
      a escola voar pelos ares. Mas como podem ver, foi uma preocupação infundada, aquela jaqueta não explodiu,
       e eu estou aqui escrevendo essa crônica.</p>

<p>Essa guerra me faz pensar na infância da minha avó, as histórias que ela sempre repete,
   por terem sido marcantes (no mal sentido), memórias que o Alzheimer luta para apagar.
    Apesar de ela ter nascido no Brasil, lembra dos reflexos que a Segunda Guerra causou no Rio Grande do Sul,
     onde ela nasceu e passou a infância.
     Apesar de não ter estudado os motivos históricos, sempre demonstra o desejo de que isso nunca se repita.</p>

<p>O que aconteceu durante a infância dela? Como vários descendentes de alemães no Brasil,
   ela nasceu em uma comunidade formada por alemães e seua descendentes, com cultura alemã. 
   Seus pais, meus bisavós, não sabiam quase nada de português, falavam com os filhos, amigos e família 
   apenas em alemão, e com os conhecidos brasileiros fazendo gestos.</p>

<p>Ela lembra que era proibido falar alemão, e que se alguém os ouvisse falando em sua língua materna,
   seriam punidos. Tinham de cochichar, falar o mínimo possível e até mesmo tentaram criar uma nova língua.
    Ela e uma irmã um pouco mais velha se encontravam no galinheiro para colocar as conversas em dia,
     violando a proibição de Vargas. Uma vez ela até contou sobre o inspetor da polícia, alemão ou filho de alemães,
      que recebera a triste ordem de informar as pessoas da Colônia de que era proibido falar alemão no Brasil.
       O irônico é que provavelmente ele teve que traduzir a lei para o alemão, já a violando.
Depois desses acontecimentos ela aprendeu o português na escola, com a ajuda e paciência de uma
 professora que não falava Alemão.</p>

<p>Foi um tempo em que culturas foram perseguidas por causa da guerra.
   Com os novos conflitos, isso está se repetindo. Não só com o povo ucraniano que
    está sofrendo uma tentativa de ser silenciado, mas também com a cultura russa
     que sofre com a indignação da humanidade ao ataque de Putin.
      Esta Crônica é apenas uma consideração sobre os impactos da Guerra na Cultura,
       em que todos os lados saem perdendo, e em sua maioria, pessoas inocentes. 
</p>

