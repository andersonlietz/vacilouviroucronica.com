                      <h1>Peixe de Estimação</h1>
	
	<img src="imagens/caldadepeixe.jpg" class="imagem">Foto de Karolina Grabowska no Pexels</img>
    <!--https://www.instagram.com/kaboompics/-->
    <p> Novamente estou aqui, em plena noite de quarta feira escrevendo esta crônica para meus
         leitores beta a lerem com um pouco de antecedência.
          Vou deixar aqui registrado que nas próximas semanas pretendo me dedicar mais cedo, 
          a fim de extrair um conteúdo melhor.</p>

<p>Hoje lembrei de uma curiosidade da minha infância que certamente dará uma boa crônica.</p>

<p>Eu devia ter uns 9 ou 10 anos quando deu uma enxurrada aqui no sítio.
     Isso acontece devido às fortes chuvas que aumentam o nível do rio que passa aqui perto,
      alagando  as plantações mais baixas.</p>
      <p> Foi em uma dessas enxurradas  que meu pai por curiosidade foi ver o nível da água nas lavouras, 
          e voltou com um lambari na mão.
           O pobre peixe havia subido junto com o nível da água e quando o nível baixou, 
           ficou preso em uma poça. Meu pai conseguiu pegá-lo com a mão, e trouxe para casa para mostrar o feito,
            pescar sem usar caniço e anzol. </p>

<p> Como era só um, além de ser de uma espécie pequena, 
    não valia a pena gastar azeite com aquele pouquinho de carne.
     Provavelmente meu pai esperava que eu  o soltasse novamente em seu habitat, 
     ou pensava em alimentar algum gato ou cachorro com carne branca.</p>
     <p> Ao ver aquele lambari, fiquei encantado com a ideia de tê-lo como animal de estimação, 
         e logo o tinha dentro de um vidro de conserva com água.
          Meus pais e irmãos alertavam que ele não aguentaria viver em água parada, 
          mas em minha cabeça, a água do vidro era a mesma do rio e não fazia ideia de porque eles
           se preocupavam com o peixe no meu “aquário”.</p>
           <p> Ele ganhava pedaços de pão, milho moído, eu trocava a água e lavava o vidro,
                mas não adiantou, no terceiro dia se não me engano, ele estava boiando de plancha. 
                Minha família confirmou que acontecera aquilo que eles temiam, o peixe morreu sufocado,
                 a água parada não continha  oxigênio o suficiente para manter aquela vida.</p>

                 <p> Acredito que houveram sugestões para se livrar do corpo alimentando algum gato,
                      mas eu havia me apegado e esse não era o fim que eu acreditava digno a um recente companheiro. 
                      Peguei  uma enxada e fiz uma pequena cova em um canto de terra perto da lavanderia atrás da casa. 
                      Fiz um breve minuto de silêncio e despedida e o enterrei. 
                      Essa é a história do meu primeiro e último peixe de estimação.
                       O pobre peixe vacilou, e agora virou crônica.
</p>
