<p>No almoço de hoje, a pauta principal foi uma região aqui do interior, onde há uma represa que foi feita para levar  água a um antigo moinho de grãos. Do moinho ainda restam o grande galpão, e os suportes onde ficava presa a roda d'água. Mas a represa de concreto e uma pequena ponte ainda permanecem quase inalteráveis e formam um espetáculo para quem passa por lá. Posso afirmar que esse espetáculo fica ainda mais bonito quando as chuvas aumentam o nível do rio, aprimoram a queda de água e transbordam por cima da ponte.</p>


<p>Eu tenho um fascínio por água e principalmente, pelas formas como o ser humano conseguiu “domesticar” ela para concluir seus propósitos. Aquelas ruínas de  moinho são um exemplo disso. Quando “tudo era mato”, alguém teve a ideia de canalizar a água do rio e usar sua força e peso para girar pás, engrenagens e por fim, uma pedra grande que triturava diversos tipos de grãos. Mas a água jorrava e continuava seu caminho, sendo usada por outros moinhos enquanto descia a serra e desembocava no mar. O percurso permanece, mas com o tempo vai perdendo essa utilidade mais rústica e ganha barragens de usinas hidrelétricas, com um processo menos transparente.</p>


<p>O processamento de grãos por moinhos foi tão popular na história da humanidade,
     que a profissão de Moleiro derivou o sobrenome “Muller” e suas versões em alemão. As pessoas ganhavam o sobrenome baseado na profissão do chefe da família, e como havia um ou mais moleiros em cada cidade, várias famílias receberam o sobrenome “Muller”, bem como: “Miller”, “Müller”, entre outros, variando devido aos erros de cartório. A quantidade de famílias que ganharam esse sobrenome chega a ser um grande desafio para quem busca as origens dos antepassados e encontra o sobrenome na árvore genealógica. </p>


<p>É o meu caso, tenho esse sobrenome na árvore genealógica, e conferindo as listas de passageiros de navios
     europeus que atracaram no Brasil trazendo imigrantes, me deparei com passageiros com o sobrenome “Muller” em quase todos os navios. Mas cresce comigo uma grande curiosidade em saber quais foram os primeiros moleiros da minha família, que receberam esse sobrenome. Podiam ser pais, avós, bisavós ou trisavós do meu bisavô, que era um Muller e domesticava a água com seus moinhos e rodas-d’água.</p>







