<p>Como escrevi em outra crônica, minha família sempre manteve e abateu porcos. 
    Geralmente comprando os leitões quando estes eram desmamados da mãe e vendidos por outros criadores.
     Mas um sonho  que minha mãe sempre demonstrou era em criar leitões novamente e poder vê-los sendo 
     amamentados pela porca. E compartilhando desse sonho, eu também tive curiosidade em saber como é ter um 
     porco que não é castrado, ou seja, um cachaço. 
     Acredito que esse sonho se deu talvez por uma admiração dos garotos e homens ao que é forte,
      másculo, violento ou protetor. Os cachaços têm fama de serem maiores, mais musculosos,
       com grandes presas e com maior disponibilidade a serem agressivos.</p>

<p>O sonho foi se tornando realidade, compramos uma porca e um porco e eles foram crescendo.
     A porca ficou doente e depois de um tempo morreu, então tivemos que deixar outra mais 
     nova crescendo para substituí-la. Depois de uns meses ela ficou prenha, a barriga foi 
     crescendo e logo tivemos de separar o casal, o cachaço ficou agressivo e buscava comer 
     toda a comida sem repartir com ela. Em uma manhã fria de inverno escutei um grunhido
      estranho vindo na direção do chiqueiro. Pensei que era um dos dois que havia fugido 
      e estava comemorando a liberdade, mas quando me aproximei mais escutei um grunhido 
      mais baixo e entendi que a porca estava parindo. Ela deu à luz quatro porquinhos e uma
       porquinha, que resistiram ao frio e já estão grandinhos.</p>

<p>Mas o cachaço que eu sonhava em ter, pode se tornar um pesadelo.
     Lembra que eu disse que eles costumam ficar violentos, mais 
     fortes e pesados? Pesado ele já está ficando, grande também, 
     e as presas já aparecem em sua boca, mas a parte do violento
      eu estou esperando para ver. Na verdade é a parte do meu sonho
       que eu dispenso e até espero que não se realize. Por enquanto 
       ele me deixa limpar o chiqueiro mas me assusta quando leva um 
       susto, pois dá uma grunhida feroz quando se sente ameaçado. </p>

<p>Talvez o medo maior ainda é que ele de alguma forma fuja do chiqueiro 
    e vá para a floresta até que um caçador lhe aproveite a carne 
    (como já aconteceu a alguns anos com um porco da minha família
     e é meio comum aqui no interior). Mas hoje me ocorreu uma
      esperança. Lembrei do pai desse cachaço quando fui comprá-lo,
       era grande mas parecia manso. Essa pode ser uma qualidade mais
        calma de porcos, e espero que esse no chiqueiro continue manso 
        e cumprindo com sua função.</p>

