                      <h1>Não, prezado vendedor!</h1>
	
	<img src="imagens/no.jpg" class="imagem">Foto de Miguel Á. Padriñán no Pexels</img>
    <!---->
    <div class="espaco"></div>
    <p>Essa é a palavra que entala na minha garganta. Sei que ela nos pouparia tempo, diálogo e argumentos.
         Mas não me sai da cabeça que a proferindo, eu estaria sendo mal educado com você. 
         Sei que tem família e precisa sustentar, entendo que o dinheiro precisa girar em várias mãos pelo bem da nação.
          Enquanto isso, vou tentando lhe mostrar os motivos de não poder comprar seu produto, não o precisar, 
          e além disso, não o querer.</p>

<p>Admiro quem exerce essa profissão, que sabe lidar com o público e contribui assim com a nossa sociedade. 
    Quem vende por bem, com interesse, empatia, e consegue perseverar mesmo com as negativas do mercado.
     Quem mata a sede de outros nos semáforos e engarrafamentos, fornece panos de prato às diligentes donas (ou donos) de casa,
      flores aos apaixonados, balas e trufas a quem quer adoçar a vida, travesseiros a quem precisa dormir bem. </p>

<p>Porém fico indignado com aqueles que nos impõem sem dó nem piedade, o que <b>nós</b> precisamos. 
Decidem por nós sem nos conhecer ou ter intimidade para isso.</p> 

<p>Aqui de vez em quando aparecem vendedores de panelas e roupas de cama. Professam a necessidade que todos 
    têm de possuir uma panela antiaderente ou um cobertor direto da fábrica.
     Um já me aconselhou comprar uma frigideira antiaderente para agradar a minha esposa. 
     Expliquei que sou solteiro, para decepção não só dele, mas de toda a classe de vendedores.
      Mas graças a ele descobri o segredo para a felicidade conjugal: panelas antiaderentes.</p>

 <p>Estou  anotando aqui para lembrar no futuro. Enquanto isso, vou usando as panelas de ferro,
      aquelas à prova de balas, que tenho medo de deixar cair e quebrar o piso da cozinha ou os pratos da pia. 
      Em um dos argumentos o vendedor alegou que com uma panela antiaderente eu não preciso usar gordura.
       Mal sabe ele que nos meus dotes culinários, ela é imprescindível.</p>

<p>Um vendedor de cobertores foi mais extremo, desconto atrás de desconto, o que nos leva até mesmo a 
    desconfiar o valor do produto. Não adiantou nem mesmo falar que minha avó era uma artesã de cobertores de lã
     de carneiro, caprichadas para aguentar o frio da Serra Catarinense. Na verdade, em vez de comprar,
      nós deveríamos nos desfazer de alguns que ficam ocupando espaço. Talvez poderíamos ter feito uma venda reversa, 
      fazer ele sair com uns cobertores a mais em vez de um a menos.
       Mas ele saiu com a mesma quantidade com que entrou, 
       vendedores teimosos passam vergonha quando encontram um alemão.</p>

<p>Como eu gostaria de me sentir livre e educado mesmo pronunciando a frase do título!
 Não, prezado vendedor!</p>

