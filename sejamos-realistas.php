    <div class="espaco"></div>

    <p>Estava aqui pensando sobre o pessimismo e os pessimistas, 
      de vez em quando eu mesmo me enquadro nessa categoria. 
      Muitas vezes o problema não está  no pessimista em si, 
      mas sim quando eles dão seus palpites recheados de pessimismo. 
      Acredito que todos nós temos consciência dos nossos problemas, suas probabilidades, 
      sabemos as suas consequências, e, em algumas vezes, como evitar e o que fazer caso eles surjam. 
      Mas você está fazendo o possível para evitar um problema ou não pensar que ele é o fim do mundo e
       aparece um pessimista encarando como se o problema já tivesse acontecido, ou pior, fazendo de conta que o problema é dele.</p>

<p>Já pensou que tanto pessimismo como otimismo são distorções da realidade? Imagina os golpes,
   um pessimista recebe uma ligação e a pessoa do outro lado da linha fala que sequestrou algum familiar. E ele pensa:</p>

<p>“ Mais um dia normal em que coisas ruins acontecem comigo.” ou:</p>
<p>“ Eu sabia que isso ia acontecer!”</p>

<p>Já um otimista ganancioso vê um anúncio de uma proposta de “investimento”, em que tudo 
  o que ele precisa fazer é mandar uma quantia em dinheiro e em algum futuro bem  breve o dobro cairá em sua conta.</p>

 <p>“Perfeito! Chegou a minha vez! Não devemos deixar escapar nenhuma oportunidade!” </p>

<p>Nem pensa que a proposta é muito boa para ser verdade. Não preciso dizer que nos dois casos há decepção. 
  Existem otimistas assim, e pior, existem os que ainda fazem o possível para “otimizar” a vida dos outros. 
  Por exemplo, os que ainda por cima acabam compartilhando as fraudes. De tão otimistas acabam envolvendo
   família e amigos em golpes. Ou provavelmente você conhece alguns otimistas que sabem o que você precisa
    fazer para ser mais eficiente. Criam o mapa com o passo-a-passo que você deve tomar, parecendo tudo muito fácil,
     á distância de uma braçada, dando a entender que: ou você é muito burro ou não quer ter êxito.</p>


<p>Já o  realismo envolve a razão, possibilidades quase estatísticas. 
  
Mas como em todas as atitudes, o principal problema acontece quando interfere na vida dos outros.
 Realistas também são odiados quando dão palpites cercados por realismo aos pessimistas,
  otimistas e até mesmo a outros realistas. Pois há lugares em que mesmo o realismo não é bem vindo.</p>

<p>Me encantam as pessoas que demonstram equilíbrio em avaliar a própria realidade, com estatísticas,
   pitadas equilibradas de pessimismo e otimismo, e de tão racionais, sabem que a realidade do outro é diferente, 
   que ele possui direito de escolha e será ele quem colherá os resultados, bons ou ruins.
    Então, pelo bem do próximo e de nós mesmos, sejamos realistas!</p>


