                      <h1> Os fardos de Jay Gatsby</h1>
	
	<img src="imagens/gatsby1.jpg" class="imagem">Arquivo Pessoal</img>
                 
         <p>
        Terminei de ler O Grande Gatsby e fiquei fascinado.
         Acredito que como leitor e cronista eu tenho direito (quase dever)
         de contar minha experiência de leitura. 
        É difícil falar algo sobre o livro que ninguém não tenha dito antes,
         pois esse romance foi uma grande obra americana, 
        pois além da qualidade literária, aborda vários aspectos históricos do ano 1920,
         época de ouro dos EUA
         após o fim da Primeira Guerra Mundial e antes da quebra da bolsa de valores de Nova Yorke.
         </p>
          <p>
       Então essa obra é abordada de diversos pontos de vista, como o histórico, filosófico e literário.
        Acredito que todas essas informações sobre, tenham colaborado para que eu demorasse a lê-la. 
            </p>
            <p>
            Serviram como uma espécie de "falso spoiler", em que eu acreditava que a leitura seria chata.
            Parecia que a estória era <i>apenas </i> sobre
            um ricasso tentando conquistar uma antiga paixão. E se você estiver se perguntando, 
            não foi apenas isso, 
            o livro é uma montanha russa, em que começa lenta e vai acelerando,
             até chegar no looping e queda livre. 
            Creio que eu teria um ataque cardíaco em uma montanha russa, mas o livro eu aguentei.
            </p>

            <p>
            A estória gira em torno de Jay Gatsby 
            (poucas pessoas sabem o primeiro nome, ou tem intimidade para chamá-lo assim).
            O narador é Nick Carraway, 
            que vai trabalhar em Nova Yorke e aluga uma casa simples ao lado da mansão de Gatsby. 
            Curiosamente, 
            ele recebe um convite para as festas na mansão.
            </p>
             <p>
            Demora até Nick realmente o conhecer, tanto pessoalmente como sua índole,
             pois Gatsby dificilmente participa de seus próprios banquetes,
              e é encoberto por várias teorias e lendas, criadas,
             compartilhadas e alimentadas pelos participantes de suas grandes festas.
             Os maiores mistérios são sobre a sua família e a origem de todo o dinheiro,
              bem como a possível estada dele em Oxford.
            </p>
            <p>
              Nos primeiros momentos do livro eu quase me arrependi de ter iniciado a leitura,
              é a parte em que conhecemos um pouco da vida do Nick, narrador e primo de Daisy,
               que também têm um papel especial na trama.
             A partir do momento em que Daisy apareceu, senti uma ansiedade para continuar o livro, 
             a minha atenção foi fisgada.
              Logo depois as festas na mansão roubam a cena, e quando Gatsby aparece,
               o livro vai ficando cada vez mais irresistível,
            é cada vez mais difícil pausar a leitura para ir dormir.
             </p>
              <p>
             O personagem Gatsby têm algumas dores, não no sentido físico, mas psicológicas. 
             Talvez você se pergunte: "Dores? Mas você disse que ele é um milionário!". 
              Sim, ele é! Mas o dinheiro não o livra de todos os males. 
             Gatsby se esconde atrás das lendas feitas a respeito dele, 
             praticamente ninguém realmente o conhece.
           Isso o ajuda a proteger sua privacidade, mas o cerca de relações superficiais,
           ninguém realmente está interessado nele, apenas no "Jazz". 
           Isso se confirma conforme a estória avança.
            </p>
             <p>
             Outra dor é a desilusão amorosa. Mesmo que Nick veja Gatsby como a esperança em pessoa,
             essa esperança torna as coisas mais difíceis. Mesmo milionário, "com o mundo à sua disposição", 
             o herói persiste em dar suas festas extravagantes, 
             hipnotizado por uma luz verde no píer da mansão da mulher amada. 
             Ele é esperançoso, mesmo tendo se passado cinco anos desde o primeiro encontro com a amada, 
             e mesmo ela estando casada e com uma filha.
             </p>
             <p>
            A um tempo atrás eu me perguntei sobre o que se tratavam os livros da minha estante. 
            Me surpreendi com a resposta: 
           Dom Casmurro? Decepção amorosa. Trabalhadores do mar? Decepção amorosa. O Grande Gatsby? 
           Nem preciso falar.
             E assim sucessivamente. Me perguntei se é um fascínio do meu inconsciente sobre livros trágicos. 
            Talvez uma incredulidade exagerada sobre os finais felizes. Mas tenho uma certa inveja de Gatsby, 
              toda essa esperança é necessária em alguns casos.
             </p>
             <p>
             Espero ter contribuído com as minhas opiniões pessoais sobre essa leitura,
              e te deixado disposto(a) a ler esse clássico! 
             </p>
               <p>  
               Obrigado pela leitura! Nos encontramos aqui na próxima semana! ;)
</p>

