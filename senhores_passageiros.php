<p>Sir Francis Cromarty, um dos personagens de Júlio Verne, se torna companheiro de viagem de Phileas
 Fogg por um tempo em sua volta ao mundo. Um encontro muito oportuno, em que o militar,  além de participar 
 de algumas rodadas de “whist” com Mr. Fogg, ainda o ajuda em um ato heróico pelo território em que atravessam juntos.
  Ambos na primeira classe do trem, e no lombo de um elefante.</p>

<p>Esse personagem me recordou de um dos muitos relatos transmitidos oralmente por meu saudoso pai,
     e muito provavelmente verídicos (eu que não vou duvidar), 
    era a desilusão que tivera aos quatorze anos ao tentar fazer a vida na cidade em que nasceu, 
    mesma cidade que deixara aos seus sete anos ao se mudar com os pais para a vila interiorana que os acolhera.</p>

 <p>Para deixar claro o quanto sua estada na cidade natal foi penosa,
      ele relatava que viajou novamente para cá de trem, não confortavelmente como fizera a viagem de ida, 
      mas ao lado de fora dos vagões, onde a viagem aparentemente econômica muitas vezes custava a vida. 
      Estava acompanhado de militares, não da patente de Sir Francis, mas sim “milicos”, também passageiros  da última classe.</p>

<p>Não tinha acesso ao vagão de refeições, então a dieta adotada pelos tripulantes consistia em espigas de milho verde,
     provavelmente confiscadas de alguma plantação. Me pergunto porque os soldados estavam naquele trem, 
     se foram transferidos para outro batalhão, ou só voltando para casa, assim como o meu pai. 
       Não faço ideia quem desembarcou primeiro, se meu pai ou eles, mas em algum lugar se despediram,
      e nunca mais voltaram a se ver. Será que ele se perguntava o que a vida reservou a esses soldados? 
      Seguiram carreira? Desistiram das forças armadas? Casaram? Tiveram filhos? 
      As únicas certezas  estavam com aqueles jovens, naquele momento, enquanto dividiam 
      espigas de milho entre os vagões do trem.</p>

