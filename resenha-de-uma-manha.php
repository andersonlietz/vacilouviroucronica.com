    <p>Em uma manhã do ano passado, por essa  mesma época, um cronista que ainda não escrevia semanalmente acordou. 
      As visitas da família rondavam pelo terreiro, estavam hospedadas em uma casa vizinha. Uma dessas manhãs tristes,
       em que não se tem ânimo de sair da cama para ir tomar café, pois o chão está minado, não de minas terrestres, 
       mas pessoas, que vão apagando as  chamas de esperança.</p>

<p>Sentou na cama e pegou o livro na mesinha de cabeceira, foi um dos primeiros que comprou e estava empolgado com sua leitura.
   A estória do livro se passa na Sicília, ilha que na época desejava ser independente de Roma, mas não conseguia se livrar
    de seus tiranos. Ilha que foi o cenário para a criação da famosa máfia Siciliana, com seus esquemas de agiotagem e outras 
    formas de ganhar dinheiro sujo. Também é palco da ascensão de Salvatore Guiliano, homem pobre e trabalhador que com o apoio
     de seu amigo Pisciotta, se torna líder de um grupo de rebeldes. </p>

<p>Vira  um Robin Hood siciliano, rouba e assalta os ricos e poderosos e divide seu despojo com os pobres camponeses que 
  eram explorados por estes. Com esses atos desafia Don Croce Malo, “chefe dos chefes”, que ganhou fama e prestígio na região
   com seus esquemas “protegendo” os ricos e poderosos com seu pequeno exército particular. É respeitado com temor pela população 
   siciliana, isso fica evidente quando contém uma multidão agitada apenas delimitando uma linha imaginária, cavalgando com seu cavalo.
    Ninguém se atreve pôr o pé do outro lado em que seu cavalo pisou.</p>

 <p>Don Croce oferece um acordo para Guiliano, mas este recusa, pois sabe que é uma forma de conter seu ímpeto de “homem do povo”.
    Continua roubando casas, assaltando caminhões de Don Croce, fugindo da polícia. Ao final, Guiliano é traído e assassinado, e, 
    como seus admiradores sobrevivem, seu traidor é envenenado na cadeia.</p>

<p>O cronista foi interrompido na sua leitura algumas vezes, por alguns gritos, dentre eles um de: “Cama foi feita pra dormir e
   não pra ficar morando”. Evidentemente pronunciado por uma pessoa precipitada, ainda grogue de ressaca, e com os intestinos se
    segurando firmes para não serem expelidos. O cronista erguia os olhos, respirava fundo e voltava ao que estava fazendo antes. 
     Hoje tem vários motivos para ler, dentre eles esta crônica, onde resenha uma manhã complexa de feriado.</p>

