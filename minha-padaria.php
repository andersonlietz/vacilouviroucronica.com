                      <h1>Minha Padaria</h1>
                  <img src="imagens/padeiro.jpg" class="imagem">
                    <p>
                 Paira no ar o boato de que a Crônica não pode ser mencionada sem juntamente mencionar o
                  Brasil,
                  e isso faz todo sentido. 
                  Pois desde Pero Vaz de Caminha, que escreveu a carta sobre o Brasil ao rei de Portugal,
                   considerada a primeira Crônica Brasileira, passando pelos ilustres cronistas Cariocas, 
                  até as crônicas de hoje publicadas nos jornais, revistas, blogs e sites,
                   o Brasil é palco e referência.
                  </p>
                   <p>
                     Seu formato permite considerações do cotidiano, quase como um artigo jornalístico, 
                   porém sem necessariamente trazer uma informação fiel ou uma "cobertura completa", 
                  tudo recheado de hipóteses e opiniões. A crônica é mais sensível,
                  então dialoga sobre características do cotidiano que passariam despercebidas do artigo 
                  jornalístico. 
                  Por exemplo, o Rio de Janeiro foi descrito em crônicas por cronistas de renome,
                  que usavam a cronica como arma contra as injustiças que passavam em  seu dia a dia,
                   como pobreza,
                  racismo, entre outras, que, creio eu, não eram mencionados em jornais de outra forma.
                 </p>
                  <p>
                    A crônica foi usada como uma quebra de tensão nos jornais, ou seja,
                   depois de o leitor ficar sabendo de vários relatos tristes, como economia ruim e violência,
                  ele encontra uma coluna no jornal com uma crônica que o entrete e o faz rir, 
                  retirando-o de uma atmosfera pessimista.
                </p>
                 <p>Assim também surgiu a rotina corrida de criação dos cronistas,
                    ou seja, criar um texto bom,
                  que prenda a atenção do público, em uma semana. 
                  Muitos se dão bem mesmo com o prazo curto, outros nem tanto. Ocorrem inconvenientes,
                 bloqueios criativos, doenças, problemas familiares, etc. 
                 Então mesmo com esses infortúnios o cronista
                 publica uma crônica não muito elaborada, para preencher a coluna do jornal.
                </p>
                 <p>
                   Em uma entrevista ao programa Roda Viva, (disponível no Youtube),
                    Fernando Sabino se compara a um padeiro:
              “Minha obra é uma padaria, eu faço pão toda noite, pra vender de manhã no jornal”.
                 Mas esse “pão”, muitas vezes subestimado, é um pão fiel,
                  que sacia a fome da manhã e é uma conversa entre
                    dois amigos, o cronista e o leitor.
                  </p>
                 <p>
                   O primeiro contato que eu lembro sobre escrever uma crônica, 
                   foi em um trabalho de Português no ensino fundamental.
                   O desafio era escrever uma crônica de memórias,
                    depois de entrevistar um idoso e descobrir as memórias dele.
                 Fiz a entrevista com a minha avó, mas sem caderninho anotando as respostas,
                  tinha vergonha e não queria que ela
                 estranhasse.
                </p>
                 <p>
                   Acredito que foi a primeira entrevista de escola em que eu realmente entrevistei alguém,
                    em vez de  criar uma entrevista e adivinhar as respostas do "entrevistado".
                   Quem nunca forjou uma entrevista para um trabalho de escola,
                    atire a primeira pedra (no rosto não).
                 A entrevista foi bem natural,
                  pois eu sempre tive curiosidade sobre a vida dos meus avós e bisavós.
                Escrevi a crônica, e tive contato com outras bem melhores, foi uma experiência incrível.
                No ensino médio conheci as crônicas do Luis Veríssimo, com seu estilo humorado, 
               talvez um dos maiores estímulos pra mim começar a escrever,
                apesar de eu não conseguir escrever com o mesmo humor que ele.
              </p>
               <p>
                 Passei a escrever alguns textos, e chamar de crônica, mas eu não sabia escolher 
                bons temas e descrever adequadamente, tenho dificuldades nesses quesitos até hoje. 
                 Mas valeu o esforço e a vergonha inicial, acredito que hoje escrevo melhor,
                apesar de ter muito a melhorar. Como um padeiro, eu vou desenvolvendo os meus pães, 
                melhorando a quantidade e qualidade dos ingredientes, ganhando prática em sovar a massa.
              </p>
                 <p>
                   Talvez o meu pão dê sabor ao despertar de alguém, acompanhado de algum café. 
                   Pode até ficar esquecido em algum armário, passar dias, semanas, anos, talvez virar um fóssil,
                  duro como pedra, como a pedra de Davi, ou a de Drumond.
                </p>
