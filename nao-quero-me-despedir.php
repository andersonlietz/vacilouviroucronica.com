<section id="content">
<p>Como diz a música “<a rel="nofollow" href="https://www.youtube.com/watch?v=XZmhLx3pzqE ">Abschied ist ein scharfes schwert</a>”,
 a despedida é uma espada afiada, que vai fundo em nosso coração. Mas arrisco dizer que o Roger se referiu à despedida dita,
  pronunciada e ratificada entre duas pessoas, e provavelmente um casal. Mas a despedida não pronunciada também dói,
   fere e marca de uma forma estranha, como um perigo iminente, a solidão repentina pronta para atacar. </p>

<p>Por mais que o momento derradeiro se aproxime, não quero me despedir, a despedida é de certa forma, 
    desnecessária. Será que ela sente o mesmo que eu? Há diferenças em nossas reflexões,
     ela deve ter sentido o mesmo em diversas vezes, e como agora está na outra fase,
      faz o possível para que isso seja para mim, de algum modo, natural. </p>

<p>Algo que me vem à mente e pode ser melhor que uma triste despedida, 
    é intensificar as ações que demonstram aquilo que eu nunca tive coragem de dizer.
     Somos tristes vítimas dessa busca intensa por virilidade, insensibilidade, que de certa forma,
      em vez de nos proteger, nos impede de falar palavras como aquele mais sincero “Te amo”, o “Te admiro”, 
      e o “Você faz bem pra mim!”, entre a família, com tão puro significado.</p>

<p>Não tenho como imaginar direito esse momento, e até evito isso. Pode ser que ela nem lembre mais do meu rosto,
     minha voz e comportamentos, e pergunte “Quem é você?”, a alguém que tanto conheceu.
     Talvez ela espere uma despedida, e acredite nunca mais me ver, mas espero que seja  um “Até Breve”, ao invés de um “Adeus”.</p>

 
