    <p>Mesmo com o tempo nublado, os cavalos foram buscados na pastagem e ganharam uma porção de milho. 
      Comeram enquanto eram envoltos por cintas de couro e peças de madeira. Ele terminou de afirmar os arreamentos,
       passou a mão na pelagem brilhante e sorriu satisfeito com seus animais. Consultava os céus,
        mas nenhum sinal de sol era visto no horizonte. Escutou passos na direção da casa, era Ela,
         que lhe trazia o casaco, preocupada que o frio e a humidade lhe fizessem mal. 
         Sentiu vontade de esbravejar, que Ela não se preocupasse, mas a compreendia e levava em conta 
         que não era mais  rapaz.</p>

<p>Vestiu o casaco, pegou as rédeas e com um “resmungo” deu o comando e fez os animais andarem.
   Caminhou alguns minutos conduzindo-os até que sentiu os pingos da chuva caindo em seu chapéu.
    Tinha que retornar, deu meia volta aos cavalos e os atiçou para andarem depressa.
     Quando chegou novamente em casa, tirou o arreamento e os prendeu dentro da estrebaria.
      Entrou em casa e recebeu ordens para que se secasse e vestisse roupas secas. Obedeceu.</p>


<p>Ela estava entretida tricotando algo na cadeira de balanço à frente do fogão.
   Ele procurou o rolo de fumo na prateleira, sentou na caixa de lenha encostada na parede e começou a processar
    cuidadosamente seu tabaco com o canivete. Já havia ganhado experiência, durante vários anos,
     maltratando o pulmão. Deixou de lado a tarefa e abriu a porta do fogão para conferir o fogo. </p>


<p>Restavam apenas brasas e a lenha da caixa havia acabado. Pegou o balde e foi pegar mais lenha na 
  pilha da varanda. Quando retornou, colocou um pouco da lenha no fogo e o restante na caixa.
   Sentou novamente, pegou o cachimbo da prateleira e deu umas batidinhas com ele na quina do fogão para tirar as
    brasas e os resquícios de tabaco velho. Depois o encheu com o fumo que havia preparado,
     pôs o cachimbo na boca e o acendeu com um fósforo.</p>
     
     <p> E assim foi se passando aquela manhã chuvosa.</p>
