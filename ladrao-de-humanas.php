                      <h1> Ladrão de Humanas</h1>
                <img src="imagens/ladrao.jpg" class="imagem">Arquivo Pessoal</img>
                 <p>
                          Estamos cansados de ver roubos por dinheiro, em que ou o ladrão rouba o a carteira de alguém, ou algo para vender.
                          Mas algo pouco comentado é o roubo por cultura, movido por algum motivo "passional". 
                          Como um observador encantado que olha para uma obra de arte, em uma galeria ou na casa de alguém, e fica tão 
                          hipnotizado que a deseja na própria sala de estar. Se ele conclui o furto, 
                          provavelmente não poderá deixar visível em casa, então a coloca na parede do porão.
                           Não vende, porque o dinheiro não o interessa, quer admirar a obra sem filas o empurrando,
                            sem limite de tempo, olhar de perto, sem as fitas impedindo sua aproximação.
                             Tocar o quadro e sentir com o tato os traços do artista.

                    </p>
                   <p>
                        É conhecido que alguém que rouba alimento para saciar a fome recebe uma medida de piedade, 
                     pois até certo ponto é algo coompreensível. 
                     Da mesma forma, o roubo à arte ou cultura recebe vistas grossas, 
                     talvez devido a uma certa fome psicológica de cultura presente no ser humano.
                    </p>
                     <p>
                         Um exemplo é a frase: "roubei mesmo, o livro estava acumulando poeira na estante e eu peguei, 
                         pelo menos vai ser útil pra mim".
                          Calma, não vou ficar julgando todo mundo, mas preciso descrever a realidade para mostrar o meu ponto de vista.
                          Um livro acumulando pó está a espera de um leitor, talvez o dono já leu, 
                          mas se está na estante provavelmente este o deixou para consultas futuras, 
                          talvez seja até um dos seus livros preferidos.
                        </p>
                          
                          <p>
                              O dono pode até estar esperando uma oportunidade de emprestá-lo a alguém,
                          é um fato que leitores normalmente gostam de conversar sobre os livros.
                          Sempre há um motivo para o livro estar na estante acumulando pó, e é um direito, livros são propriedades.
                          Peça emprestado (e devolva!).
                        </p>
                       <p>
                            - "Mas eu tenho vergonha!" (vergonha é roubar).
                        </p>
                        <p>
                            - "Ele(a) não me emprestaria."
                        </p>
                          <p>
                              Porquê? Nesses casos insista ou empreste de outra pessoa.
                          Precisamos normalizar o empréstimo de livros, 
                          e talvez a maior dificuldade quanto a isso é que muitas vezes eles não são devolvidos.
                        </p>

                          <p>
                              Poderíamos criar uma cultura em que até os mais egoístas se sintam bem emprestando seus livros.
                          
                          A Cultura e arte são incríveis, ajudam várias pessoas de várias formas, como entretenimento, para descobrir
                           coisas novas, 
                          aprender uma nova língua, etc.
                          Mas se ela é tão preciosa, porquê o roubo a ela muitas vezes é justificado? Uma das respostas é: 
                           </p>
                           <p>
                               Ela não tem dono.
                           </p>
                           <p>
                               Quem faz a arte a lança ao mundo, cria conceitos que serão copiados, inspira novos artistas,
                            (sem contar a pirataria e plágio ). Sabe aquelas frases que colocam em posts motivacionais,
                             dando referência a Chorão, Will Smith, Confúcio, Abraham Lincoln, Aristóteles, Buda, Bukowski ou até Jesus, entre outros?
                            Foi criado o conceito de que tudo o que eles falaram era algo inteligente (no caso de Jesus eu não duvido),
                             mas hoje em dia seus nomes são associados a frases que eles odiariam. 
                             Da mesma forma hoje em dia vários escritores famosos são surpreendidos por textos
                              forjados e publicados dando autoria ao seu nome. E infelizmente,
                               muitas vezes a arte é manipulada por motivos políticos.
                          </p>
                            
                             <p>
                                 Quantos quadros da Mona Leonardo Da Vinci pintou? Um.
                              Quantas cópias existem? Milhares. 
                                 Me pergunto se ele ficaria feliz em ver crianças na escola rabiscando o conceito da sua obra prima.
                                A disponibilidade de monalisas no mundo torna a original inútil? Não. Se alguém a roubasse 
                                (que isso nunca aconteça),
                                a arte sofreria uma perda irreparável, nenhuma cópia preencheria a lacuna completamente. 
                          </p>
                          <p>  
                              Há uma idéia popular de que a arte é livre, para alguns por ser 
                            preciosa demais (como a água ou oxigênio), para outros como o lixo ("por que pagar por isso?").
                            É algo ruim? No primeiro caso não, a arte é sim, preciosa e necessária. Mas sempre há preços a serem pagos. 
                            O tempo do artista, seus materiais, editoras e galerias ambiciosas, no caso dos livros o papel,
                             impressão, divulgação, etc.
                            E infelizmente quem pretende viver de arte precisa burlar preconceitos dos pais, dos amigos, da sociedade. 
                            De que arte é apenas lazer, não dá dinheiro, entre outras. 
                        </p>
                            <p>
                                Então pelos benefícios da  arte, persistamos em proteger a Mona Lisa (de preferência a original),
                                 apoiar artistas, comprar obras!
                                Com consciência tranquila e felizes por ter contribuído, para que a arte continue mudando vidas
                                 e sendo como é: livre (no bom sentido). 
                          </p>

  

