<p>Era um casal feliz, mas ela passou a ter motivos para desconfiar. Sabia dos gastos, o que entrava e saía, mas ao perguntar sobre uma certa demora dele após o trabalho e um gasto de dinheiro, ele afirmava que  havia engraxado os sapatos. </p>
<p>— Mas como isso, a graxa está tão cara? — ela perguntava.</p>
<p>— Gorjeta! — dizia ele com um sorriso discreto.</p>

<p>Sentiu que devia ter algo de errado, aquilo não lhe cheirava bem, e não desejava irritá-lo com suas perguntas. Notou a frequência em que ele demorava mais do que de costume e decidiu que deveria verificar com seus próprios olhos e, se suas suspeitas amargas se concretizassem, o pegaria no flagra. Em um dia em que ela calculou que ele demoraria, entrou em uma lanchonete perto do trabalho do marido e sentou em uma mesa a fim de esperar. Deu o horário, alguns minutos ele saiu e ela o seguiu com os olhos por um instante, depois passou a segui-lo a uma distância. </p>

<p>Ele pegou uma quadra que não era o caminho de casa e ela viu que seus cálculos estavam certos. O marido entrou em um sebo e saiu com uma revista em mãos. Sentou em um banco ao lado de fora do estabelecimento e um menino se abaixou à sua frente.
Meteu os olhos na revista e passou a ler, enquanto o garoto tirava caixas de graxa, panos e uma escova da mochila. Ela estava surpresa, o que ele dizia era realmente a verdade até o momento. Ficou observando de longe, os sapatos seriam engraxados logo e ele poderia passar em outro lugar depois. </p>
<p>O engraxate estava empenhado passando caprichadamente a graxa nos sapatos. O homem falava algumas coisas e o jovem concordava com a cabeça, às vezes balançava em negação, mas a conversa estava animada. Em alguns momentos ele interrompia a leitura e abaixava a revista para que o rapaz visse. Analisava de vez em quando o trabalho do jovem e fazia sinal com a mão para que ele escovasse os sapatos mais um pouco. Depois de um tempo tudo se repetia, logo o jovem passava ainda mais graxa e escovava novamente. Vendo isso, a mulher entendeu porque os sapatos do marido brilhavam como os espelhos. </p>

<p>Depois de um tempo o homem conferiu o trabalho do garoto e deu um sorriso, estava feliz com o polimento, logo os equipamentos foram colocados novamente na mochila, o homem se levantou e entregou a revista ao rapaz. Tirou a carteira do bolso e pagou com algumas notas generosas. O menino as guardou, sentou no banco e passou a ler a revista. O homem tomou o caminho para casa e a esposa teve de se esconder. Ela decidiu conversar com o garoto, reparou que a revista era especialmente de carros,  interrompeu sua leitura:</p>
<p>— Esse homem sempre vem aqui?</p>
<p>— Sim, meu melhor cliente. Porquê?</p>
<p>— Nada, apenas curiosidade.</p>
<p>— Não cresça os olhos nele não, da forma que fala da esposa, parece muito bem casado.</p>


