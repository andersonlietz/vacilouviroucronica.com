    <!---->
    <div class="espaco"></div>
    <p>Havia uma época em que eu me empenhava em fazer minúsculos jarros de argila, 
      provavelmente influenciado por alguma propaganda ou programa de televisão.
       Mas não tinha talento algum nisso, não sabia qual barro escolher, e quando escolhia um em alguma vala, economizava,
        fazendo peças ainda menores do que as adequadas para uma criança. E talvez o pior, não tinha paciência 
        (por incrível que pareça, leitores que me conhecem), tanto para amassar o barro, como para deixar secar. </p>

<p>De alguma forma eu sabia que as peças de cerâmica passam pelo fogo para ficarem prontas,
   mas na época eu não fazia ideia de que elas deviam secar completamente primeiro.
    Então as colocava no forninho do fogão a lenha, o que testava a paciência da minha família.
     Também em algumas vezes coloquei as peças de argila no sol, mas em todas essas tentativas elas rachavam
      e voltavam a ser apenas terra e pó. Praticamente naquela época eu e um amigo mantínhamos uma confeitaria de
       bolos de barro debaixo do assoalho da casa dos meus pais, e se você estiver se perguntando, não, nós não comíamos os bolos.
        Fizemos até algumas fogueiras em alguns casos e queimemos aqueles bolos pensando que os estávamos assando,
         eles rachavam igual os meus jarros de argila.</p>

<p>Mas guardo com carinho o pouco que o meu pai me ensinou sobre argila. Primeiro ele me explicou a diferença entre a verdadeira argila
   e o barro. Quando estávamos perto de alguma escavação com máquinas ou limpando as valas no meio das plantações, ele apontava para
    a argila e falava:<p>

<p>— Essa é a argila, boa pra fazer os pelotes de estilingue!</p>

<p>E ele ainda me mostrou como fazer, não lembro se era na época em que ele já não podia mais trabalhar, 
  pegamos a argila de algum lugar e me tornei seu aluno, acompanhando os passos daquele homem que outrora foi menino, 
  ensinando o filho a fazer projéteis de estilingue. Os que eu havia tentado fazer sem ele foram um fracasso, 
  de terra e secos ao sol, se esfarelaram ainda no ar quando atirados com o estilingue.
   Ele me mostrou que a argila deve ser bem preparada, amassada com paciência, até chegar a uma textura razoável e então,
    ser transformada em esfera. Depois desses passos terapêuticos, a minha paciência foi exercitada quando ele disse que a
     argila deve secar na sombra. </p>

<p>— Nada seca na sombra! — pensei, o que demonstra que eu sabia realmente, pouco da vida.</p>

<p>Mas descobri que a argila seca sim na sombra, se aprimora, como o vinho de reserva na adega e outras tantas coisas.
   Depois de alguns dias, que foram demorados para esse menino ansioso, pude testar os projéteis de argila.
    Diferentes dos outros que se esfarelaram no ar, estes ficaram bem sólidos, a ponto de aguentarem os meus pelotaços nas 
    paredes dos galpões e ainda recochetearem intactos.</p>

<p>Meu pai incentivava a caça aos pássaros, mas abominava a ideia de que pudéssemos matar Joões de barro ou destruir 
  suas casas com o estilingue. Era seu pássaro favorito, ele os admirava. E não precisava se preocupar comigo, minha
   apontaria sempre foi péssima, ao contrário de outros meninos, quebraria uma janela na terceira ou quarta tentativa,
    que eu lembre nunca experimentei essa traquinagem. Mas depois de tantos tiros de estilingue, não duvido que alguma
     telha ou janela foi atingida por uma “pedra perdida”.</p>

<p>Voltando ao assunto principal desta crônica, acho incrível essa arte de transformar a argila em utensílios domésticos, 
  de decoração, tijolos, casas, e é claro, projéteis de estilingue. De algo tão despercebido, mole e desprezado,
   pessoas fazem coisas úteis, lindas e com significado. Ou sem significado mesmo,
    como os meus jarros e bolos de lama da minha infância.</p>

