<p>Eu não sei você, mas eu acho as aranhas feias. Tá, de vez em quando têm umas bonitas, 
    mas em sua maioria elas me impõe medo e nojo. Quando vejo uma na televisão já começo a me coçar.
Há uns anos atrás quando via uma aranha em casa eu chamava a minha mãe. Lá vinha ela, pegava o chinelo e
 <i>pá</i>, e a aranha virava uma gosma nojenta com perninhas grudadas na parede ou no chão.</p>

<p> Agora não posso chamar minha mãe a cada uma que eu vejo, preciso mostrar coragem, em vez de um homicídio,
     a situação vira uma verdadeira batalha. Antes as aranhas ficavam paradinhas, deixavam chegar perto,
      eram tão inocentes. Agora ao desconfiarem das minhas ideias sombrias já se escondem ou ficam em um lugar 
      completamente impossível de acertar a chinelada. A penúltima estava no banheiro, duas ou três vezes a encontrei
       à noite, todas as vezes procurei algo para a acertar, mas ela já corria para se esconder atrás do armário.
        Até que um dia a vi morta e sendo devorada pela minha gatinha (gatinha literal, uma gata filhote).</p>

 <p>A outra apareceu na pia da cozinha a uma semana atrás, substituindo outras que lá apareceram mas felizmente
      viraram gosma depois de umas chineladas. Mas dessa vez as minhas chineladas não foram eficazes, 
      ela se escondia atrás da louça suja. Desisti, e em outro momento quando fui tomar água a vi dentro do ralo da pia,
       tentei matá-la afogada mas descobri que as aranhas sabem nadar melhor que eu. A água secou e ela estava novamente
        no ralo da pia, provavelmente rindo de mim. </p>

<p>Mas tive a brilhante ideia de a prender ali colocando uma panela em cima. Funcionou, ela ficou aprisionada na pia, 
    e a minha primeira intenção era matá-la de fome. Depois coei um cafézinho e coloquei na pia o coador pingando aquela
     água quente cafeinada que sobrou. Pensava que assim ela morreria queimada. Depois de uma semana naquele ambiente úmido,
      pensava que se ela não tivesse vindo a óbito de fome, queimada, provavelmente morreria de pneumonia.
       Também não fiz questão de lavar a louça, afinal, tinha uma aranha presa na pia e lavar a louça seria uma tarefa radical. </p>

<p>Depois de uma semana, quando precisei da panela novamente para fazer pipoca, fui fazer a exumação do cadáver,
     mas para minha surpresa ela estava vivinha da Silva. Peguei o chinelo mas percebi que essa tática  já é ultrapassada,
      então fui pegar a vassoura. Fui descendo o cacete, ou melhor, descendo a vassoura,
       estava até com medo de quebrar a minha caneca favorita que estava dentro da pia junto com a aranha e a panela.
        Mas algumas vassouradas foram necessárias até que a vi toda encolhida, sinal de que suas forças foram extintas
         com a minha vassoura. Peguei a pazinha de lixo e joguei seu corpo no quintal, sabendo que suas primas, as formigas,
          fariam o sepultamento. Depois lavei a louça e fiz a pipoca em paz. </p>

<p>São duas historinhas para te alertar sobre a revolução das aranhas. Se você já leu A Revolução dos Bichos, sabe do que 
    estou falando, é comum os animais se comunicarem e criarem essas conspirações. Penso que as aranhas já devem ter elegido um líder,
      desenvolvido uma bandeira, um hino, e estabelecido uma sede com academia e estande de tiro. 
      Estão se preparando para a grande vingança, mas eu tenho a minha 
     vassoura, vão todas virar uma gosma nojenta na parede, lutarei até o fim.</p>


