                      <h2>Crônica Musical</h2>
	
	<img src="imagens/som.jpg" class="imagem">Foto de Anthony no Pexels</img>
    <!--https://www.instagram.com/inspiredimages_s6/-->
    <p> 
    Às duas horas da madrugada de domingo acordei com um som infernal no campo de futebol aqui perto.
     Na hora lembrei da minha avó e minha mãe que provavelmente haviam acordado também com aquela barulheira.
      Acreditava que o meu tio e a família do meu primo estivessem dormindo na casa da minha mãe. 
      Não queria que ninguém agisse precipitadamente, como da vez em que o meu tio,
       depois de umas cachaças, ameaçou os motoqueiros na pista de motocross aqui a frente.
        Mesmo desejando que ninguém agisse precipitadamente,
         confesso que desejava ir lá e colar a orelha do infeliz no aparelho de som. 
</p>
<p>
  Depois de uns minutos o barulho parou repentinamente e eu ouvi um:  
</p>
<p>
   — Tá bom! Tá bom! </p>
<p> 
  Acho que algum vizinho irritado vestiu o manto sagrado da diplomacia e fez ele engolir aquela porcaria.
</p>
<p> Essa situação me fez lembrar de algo que eu já queria escrever há muito tempo.
Trata-se de um acontecimento ocorrido em São Bento do Sul há muitos anos,
 mas que pode ter acontecido muitas vezes e em diversas partes do mundo.</p>
<p> A banda local estava desfilando e trazendo alegria à cidade,
   quando em um determinado momento parou de tocar e seus membros passaram a andar em silêncio. 
   Depois de um tempo retomaram o trabalho com seus bumbos, trombones e demais instrumentos. 
   Porquê eles fizeram silêncio? Foi um ato de respeito à viúva  e demais parentes do proprietário da casa,
    que havia falecido no dia anterior.</p>
<p> Outra época, é claro, mas esse respeito me comove. 
  O maestro e os demais poderiam pensar:</p>
<p> — Somos a banda da cidade! Nada nos detém!
   E talvez eles se sintam confortados com a nossa música,
    todo mundo gosta de uma boa Polka!</p>

<p>Mas não, prestaram as condolências de uma forma muito cavalheiresca! </p>
<p>Isso que eu acho incrível nas músicas mais clássicas:</p>
<p>Ouve quem quer!</p>
<p>
  Você já foi acordado por alguma valsa de Strauss às duas da madrugada? Talvez Beethoven? 
  Se você for do sul do Brasil talvez pode ter presenciado pertubação do sossego com alguma das músicas da Oktoberfest.
</p>
<p> É raro, mas acontece.
</p>
   <p> Pode ter passado uma tarde inteira ouvindo "Ala pucha tchê!", 
   "Do fundo da Grota", ou até a "Lembranças" do Porca Véia no som de algum vizinho,
    sem vontade de ir reclamar porque provavelmente ele estava cercado de espetos, 
    garfos e facas de churrasco (em alguns casos até mesmo um .38).
</p>
<p>
  Eu mesmo me vejo constantemente abaixando o volume da TV para não perturbar os vizinhos com as
   minhas playlists de velha. Sim, gosto de algumas músicas bem peculiares.</p>
  <p>Aproveitemos essa maravilha da humanidade que é a música, mas pelo bem de todos, sejamos equilibrados,
    sem acordar outros às duas da manhã.
</p>

