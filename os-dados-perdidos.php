<p>Ergueu a cabeça ainda meio tonto, observando seu próprio corpo ali deitado no asfalto.
     As costas, a cabeça, bem como várias partes do seu corpo estavam doloridas. 
     O barulho dos carros começava a aumentar novamente, ainda mais o risco de ser atropelado se
      continuasse ali dormindo na rua. Apalpou as pernas procurando as alças da pasta para se levantar,
       mas não as achou com o tato, foi obrigado a encarar a luz ofuscante do sol e constatar que realmente,
        a pasta não estava lá. Olhou ao redor, nada dela.</p>

<p>— Mas o que aconteceu na noite passada? — se perguntava, por mais que sabia a resposta:</p>
 <p>— Uma noitada, uma maldita noitada!</p>
<p>O que lhe desesperava era a perda da pasta, e dentro dela o pendrive com os dados pessoais de toda cidade.
    Uma grande falha na segurança.
     Grande funcionário público que era, como pôde expor tantas famílias ao perigo? 
     Quantos cairiam dali em diante em golpes? Não! Precisava achar a pasta. Pretendia falar com o amigo,
      que saberia sobre o paradeiro dessa pasta. Mas onde estava o celular? Provavelmente, 
      adivinhe, na pasta a ser procurada. Poderia pedir o celular de alguém emprestado,
       ou ligar de algum telefone público. Mas adiantava se não lembrava o número?</p>

<p>A irmã devia ter o número do amigo, ela que os apresentou. Foi até a casa dela,
     pegou o celular emprestado e ligou para o número do amigo.</p> 
<p>— Alô! — uma mulher atendeu.</p>
<p>— Posso falar com o João?</p>
<p>— Provavelmente você está se referindo ao dono desse celular que alguém deixou ontem a noite aqui em casa.</p>
<p>— Onde você mora?</p>
<p>— (Cidade Vizinha).</p>
<p>— Tem uma pasta de couro marrom aí? Eu não faço ideia do que aconteceu ontem e perdi ela em algum lugar,
     está com todos os papéis do meu trabalho.</p>
<p>— Talvez ela esteja aqui, e talvez não, mas podemos negociar!</p>
