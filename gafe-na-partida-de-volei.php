<p>Era mais uma aula normal de educação física no ensino médio, 
    na época em que o professor estava focando bastante nos jogos de vôlei em vez do tão costumeiro futsal. 
    A classe foi dividida em dois times e o professor ficou apitando o jogo. Eu não lembro muito daquela partida, 
    mas provavelmente meu time estava perdendo, como de costume.</p>

<p>Em uma das últimas jogadas, lembrei de uma estratégia meio suja de um amigo para essas ocasiões.
     Quando formávamos um grupo de crianças e íamos jogar futebol no gramado da casa dele, o malandro 
     apelava para a falsa flatulência. Era só alguém estar com a bola perto do gol e a milésimos de chutá la
      que ele colocava o antebraço na boca e soltava um:</p>


<p>— FRRRUUUMMMM </p>


<p>O jogador perdia a compostura séria e focada e com um chute desengonçado chutava a bola para qualquer lugar, 
     menos para o gol. Nós do time adversário sofríamos por perdermos gols de uma maneira tão banal e ao mesmo 
     tempo ríamos muito devido ao som tão humorístico. </p>


<p>Mas voltemos à partida de vôlei com a minha turma no ensino médio. Lembrei daquela técnica adotada pelo meu
     amigo para atrapalhar o adversário, e fui me preparando para usá-la. Precisava determinar o momento exato,
      não poderia ser nem muito cedo nem muito tarde, mas na hora em que um  adversário estivesse dando um grande 
      passo e poderia se atrapalhar com a minha brincadeira e perder um ponto. Decidi que o momento mais propício
       seria quando alguém do time adversário fosse sacar a bola.</p>


<p>Estava chegando a hora, fui me concentrando para quando alguém fosse sacar, eu colocasse o antebraço nos
     lábios e fizesse o estrondo de pum. Logo chegou o momento, alguém foi sacar, eu coloquei o antebraço nos lábios,
      fiz a pressão de ar e:</p>


<p>— FRUUUMMM</p>

<p>Mas eu não havia calculado a distância, o outro jogador estava muito distante e por isso não escutou o estrondo,
     deu impulso e aquele gracioso tapa na bola, que veio voando até o nosso lado da rede, mas todos do meu time 
     estavam distraídos com o estrondo e as gargalhadas do meu colega, que ouviu e não se conteve,
      chegando até mesmo a deitar no piso da quadra. Perdemos aquele saque com a distração que eu havia 
      preparado para o time adversário. Confesso, foi uma péssima estratégia, mas valeu pela aventura e as gargalhadas, 
      mas o pior foi terem acreditado que eu realmente havia soltado um pum.
       Não estavam acreditando na minha explicação, e a minha verdade acabava sendo encarada como desculpa para esconder tamanha gafe.
        Por pouco não ganhei fama de “Peidão” pelos corredores e salas da escola. </p>

 
