    <p>Como o sítio da minha família é razoavelmente perto da vila,
         tive uma certa liberdade quando ganhei minha segunda bicicleta.
          Já tinha uns nove ou dez anos e a região em que eu moro não costuma ter muitos episódios trágicos,
           estes acontecem de vez em quando, mas são típicos de uma cidade interiorana.
            Provavelmente foi naquela época que meus pais me permitiram pela primeira vez ir sozinho na
             chácara da minha avó, que fica a uns quatro quilômetros na direção oposta à vila. 
             Depois disso, passei a fazer compras sozinho, e ia muitas vezes levar recados e
              outras coisas para a minha avó e tio que viviam no alto do morro.</p>

<p>Certa vez, voltando da vila para casa, em uma parte do trajeto em que há poucas moradias,
     me ocorreu o roubo meio sucedido. Um casal apareceu,
      não me recordo se estavam indo na mesma  direção, ou se iam para a vila.
       O homem se pôs na minha frente e não lembro se parei para não atropelá-lo ou se 
       ele mesmo segurou o guidão da bicicleta para me deter. Repetia suas desculpas,
        uma atrás da outra, estava desesperado, seu rosto pingava suor. Não sei se estava sob
         efeito de alguma droga, mas tinha o desespero igual o de um pai indo salvar um filho da morte.
          Quando coloquei meus pés no chão, ele puxou a bicicleta, repetiu suas desculpas e se perdeu na distância,
           na mesma direção da minha casa. Da mulher eu só lembro as palavras, que que repetia na tentativa de impedir o marido:</p>
<p>— “Esse não! Ele é um Lietz! O pai dele é o Hilário!”</p>
<p>Essa frase retumba na minha cabeça, mesmo depois de uns dez ou onze anos,
     como se o meu sobrenome fosse um dos sobrenomes das cinco famílias de New York (Família Colombo, 
     Bonanno, Gambino, Genovese e Lucchese ).</p>

<p>Mas não adiantou, ele  levou embora a bicicleta vermelha. 
    Confuso, decidi voltar para casa caminhando.
     Cogitava a possibilidade de nunca mais ter de volta a bicicleta que havia sido comprada pelo meu irmão.
      A mulher, provavelmente envergonhada, não deu explicações e continuou o seu caminho.
       Caminhei mais ou menos até a metade do trajeto até a minha casa, 
       quando vi novamente o homem voltar. Me devolveu a bicicleta,
        pedindo mais desculpas e sem dar explicações. Fiquei ainda mais confuso.</p>

<p>Quando cheguei em casa, contei isso ao meu pai, e ele disse que estava como de costume sentado
     ao lado da estrada olhando o movimento, quando viu o homem que estava se aproximando com a minha bicicleta.
      Disse que o homem caiu, se levantou e deu meia volta pedalando novamente em direção à vila. Sempre fiquei desconfiado com essa versão, 
      apesar de meu pai ser muito pacífico, não descartei a possibilidade de ele ter dado 
      uma bronca no maluco e mandado ele devolver.</p>

<p>O homem morava aqui  vila e tinha parentes aqui no interior, inclusive um amigo do meu pai. 
    Antes desse ocorrido já havia visto ele algumas vezes, e continuei a vê-lo depois, 
    mas nunca recebi uma explicação, ninguém mais tocou no assunto e eu tenho essa bicicleta até hoje. 
    Então está aqui um roubo que foi consumado em parte mas não levado adiante,
     mais um desses roubos meio sucedidos.</p>

