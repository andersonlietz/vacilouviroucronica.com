                      <h1> Saudades da Chalana</h1>
	
	<img src="imagens/chalana.jpg" class="imagem">Foto de Francesco Ungaro no Pexels</img>
         <p>
         A um quilômetro e meio da minha casa, me encontrava uma vez, vendo um cavalo deitado em uma vala ao lado esquerdo da estrada.
          Na minha inocência o cavalo havia se afogado na água que descia pela vala. Hoje em dia eu sei que é quase impossível,
           mas naquela época minha avó me assustava com a possibilidade de alguém se afogar em até mesmo um palmo de água.
            Era um animal saudável, não estava doente e ninguém previa que ia morrer de uma hora para outra,
             embora sempre haja esses infortúnios.
            </p>
        <p>
        Não era um cavalo, era a égua Chalana, último equino pertencente a minha família. 
        Eu até pensava que a música da qual meu pai tirou o nome para dar a ela era sobre uma égua. 
        Mas para minha surpresa, a música tocada  por diversos artistas fala sobre uma mulher.
    </p>
            <p>
            Mas voltando ao dia em questão. Eu estava com meu pai e meu irmão, nem sei porque me levaram junto naquele enterro, 
            não ia poder ajudar em nada e era um momento triste.
             Talvez fosse um daqueles momentos em que os pais sentem a necessidade de mostrar aos 
             filhos uma fatia do sofrimento que passarão ao longo da vida, um choque de realidade mesclado com amor paterno ou materno. 
             </p>
             <p>
             Em pouco tempo chegou o dono da serraria com seu CBT, trator que na época era sinônimo de força, 
             hoje em dia a fama é por não ter freio (mas ninguém ligava para isso naquela época).
              Era o trator usado para arrastar as toras de madeira na serraria. Mas naquela ocasião foi usado para outra tarefa, 
              ele foi estacionado perto do cavalo, e com cordas ou correntes (um dos detalhes que eu não lembro)
               os pés da égua foram amarrados.  O trator foi arrastando o corpo naquela estrada de chão,
                até um lugar em meio a floresta do homem que havia pegado a égua emprestada do meu pai.
                 Até tenho uma vaga impressão que lembro do dia em que ele veio pedir a Chalana emprestada do meu pai e 
                 nós fomos buscá-la no pasto.
             </p>
             <p>
             Dessa parte em diante não lembro mais de muita coisa, estava brincando entre as árvores e explorando o terreno.
              Voltamos para casa e passamos pelo pomar da minha avó que ficava aqui perto. Era época dos Caquis,
               e minha mãe e mais alguém estavam os colhendo, acredito que eram minha irmã e talvez  um primo. 
               Lembro de subir nos pés e me deliciar comas frutas suculentas.
                Agora as árvores do pomar cederam espaço para a casa do vizinho,
                 restam apenas um pé de laranja e um de pêra, que tiveram a sorte de estarem do lado de cá da divisa.
            </p>
            <p>
                Depois que fiquei mais velho fui descobrindo mais a respeito daquele dia.
                 O amigo do meu pai estava colhendo milho e pediu a égua emprestada para levar o milho da plantação até o galpão.
                  Colocava o milho em um cargueiro no lombo da chalana.
                   Pelo que eu ouvi ele era cruel com seu chicote e em uma dessas surras ela tentou escapar subindo o barranco.
                    Não teve força para terminar de subir e caiu no chão fraturando o pescoço.
                     Provavelmente morreu na hora ou agonizando, pois eu mesmo não havia notado sangue na água da vala, 
                     então não teve tiro de misericórdia ou sangria. Meu irmão que colocou a mão na massa abrindo a cova comenta
                      que sofreu com uma decisão precipitada do dono do sítio, 
                      pois este indicou um lugar em que em outra ocasião já havia sido usado para enterrar outro cavalo.
                       Então ao abrir a cova ele se viu confrontado com o corpo do outro cavalo em decomposição e cheirando carniça.
                    </p>
                    <p>
                    Depois disso meu pai não comprou mais cavalos, para minha tristesa, 
                    pois cresci com praticamente todos os meus sonhos de infância envolvendo esses animais magníficos. 
                    Pouco conheci a Chalana, apenas naquele triste momento. Mas a Chalana vacilou, e de saudades, virou Crônica!

                    </p>

