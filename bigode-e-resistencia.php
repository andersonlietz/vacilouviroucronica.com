<section id="content">
    <p>Vai fazer dois anos que deixei meu bigode crescer, e repare uma coisa, eu disse deixei.
       Não precisei implantar ou colar fios, nenhuma operação estética, apenas deixei crescer,
        foi completamente natural. Parei de barbear a região acima da boca, só. Ontem recebi um elogio, 
        uma conhecida que fazia tempo que não me via demonstrou uma surpresa agradável em me ver de bigode, 
        fiquei lisonjeado. </p>

<p>Sempre achei esse ornamento interessante, meu falecido pai tinha e meu irmão deixou 
  o dele crescer depois de casar.  Com o começo da pandemia, eu saía poucas vezes do sítio 
  e decidi deixar o meu crescer para experimentar. Nesse ponto o bicho pegou. Eu não tinha 
  plena consciência da repressão que os bigodudos sofrem da sociedade. Não lembro de ninguém 
  me chamando de “feio” na cara dura antes de deixar o bigode, mas agora virou algo rotineiro.
De imediato começaram as piadas, que me agradam até certo ponto, pois tenho senso de humor,
 mas por outro lado, existem frases completamente sem graça, como forma de manipulação. Vou dar alguns exemplos. </p>

<p>Alguns temendo que o bigodudo não saiba, afirmam que o bigode, como os outros  pelos,
   é anti higiênico (então raspa a cabeça). Pode ser, eu não duvido, mas em comparação com o quê? 
   Você provavelmente também já presenciou coisas tão anti higiênicas que os bigodes passariam despercebidos
    se não fossem essas pesquisas. É um comentário que coloca o bigodudo no patamar dos porcos, e quem o faz
     se coloca como o ser mais puro que já existiu. Essa não é uma tática suja? </p>

<p>Acredito que o comentário mais comum é do tipo: “Eles dois te seguram e eu raspo esse bigode!” 
  Eu nem preciso me delongar muito nesse tópico, frases assim me fazem lembrar de humilhações praticadas
   em guerras e em vários tipos de conflitos humanos. Me lembro de  Dalila que cortou os cabelos de Sansão, e os homens de Davi que tiveram 
   suas barbas e vestes cortadas pela metade, ou seja, claros atos de humilhação.</p>

<p>É como se eu tivesse uma aberração em meu rosto, uma bomba relógio prestes a explodir,
   que deve ser arrancada e jogada fora o mais rápido possível. Uma bomba que ameaça todos ao meu redor.</p>

<p>Eu imagino alguns atestados de óbitos bizarros: “Morreu de bigode”, “Morreu de infecção devido ao bigode”, 
  “Seu bigode enrolou na hélice do barco e ele foi desfigurado”, “foi saltar do avião e o paraquedas prendeu no bigode”,
   “levou um tiro por causa do bigode”, “o bigode pegou fogo e os bombeiros não conseguiram apagar”,
    “engasgou com um fio do bigode”. E em todos esses velórios alguém vai falar: “Era gente boa, mas esse bigode,  francamente!”.</p>

<p>Eu não me identifico bem com bigode. Com ele muitos sem me conhecer têm opiniões precipitadas, 
  uns talvez me considerem um palhaço, outros  acham que eu sou como aqueles policiais das antigas 
  que deixavam o bigode crescer depois de matar o primeiro bandido. 
Gosto desse ar de seriedade que ele passa, mas tenho medo porque não sou tão sério ou responsável 
como outros bigodudos são e esperam dos outros. </p>

<p>Acho interessante o tal envelhecimento causado pelo bigode e a jovialidade de um homem barbeado.
   Já acontece de acharem que eu tenho o dobro da minha real idade, e eu queria ser apenas o que sou,  um jovem bigodudo. </p>

<p>Algumas vezes quando olho no espelho e me acho feio, lembro que já sentia isso antes de o deixar crescer,
   então se eu tirar, provavelmente nem mudará muita coisa quanto há minha auto-estima. Agora as meninas
    me olham na estrada e não conseguem segurar os risos, melhorou um pouco, antes elas faziam de conta que eu não existia. 
    E durante as conversas eu me pergunto: “ele está olhando nos meus olhos ou para o meu bigode?” Ninguém mais fala dos 
    meus olhos azuis. E ainda que o meu mustache é quase transparente.</p>

<p>Mas provavelmente você está se perguntando: “se é tão ruim, porque não raspa?” É a minha resistência. 
  Irei passar uma imagem que não quero passar. A que conclusões chegarão se eu o fizer?
   Que todas essas opiniões precipitadas estão certas. Vou passar pela mesma pressão que quando deixei o bigode crescer, mas um pouco diferente.
    Será um bigode a menos no mundo para resistir a essa 
   pressão, deixarei meus colegas  bigodudos solitários na linha de frente. Terá mais mães implorando de joelhos aos
    filhos para que pelo amor de Deus não deixem o bigode crescer, pois é “algo sujo e deplorável”. </p>

<p>A ideia que eu já tenho antes mesmo de o deixar crescer é trabalhar para um dia alguém dizer a meu respeito:
   “Sério que ele tem bigode? Nem reparei.” Assim nós os bigodudos vamos resistindo, porque sabemos que os bigodes são,
    entre os problemas da humanidade, os menores.</p>


