    <p>Três anos, uma semana e alguns dias, para ser mais exato.
Naquela manhã o fusca amarelo começou sua jornada, do nordeste catarinense em direção ao norte do Paraná.
 Meu irmão no volante e eu segurando em vão o sono, fazendo a vez de copiloto. 
 Fui acordado uma ou duas vezes, ouvindo a voz brincalhona me lembrar que eu não estava conseguindo 
  cumprir com as obrigações que o meu papel exigia.</p>

<p>Durante o caminho houveram algumas paradas em obras na pista, uma para lanchar e ir ao banheiro,
     e outra em que o fusca começou a fazer um barulho fora do normal, que foi devidamente consertado pelo meu irmão.
      Era uma porca de parafuso que a gente não fazia ideia de onde saiu, 
      e estava sendo jogada de um lado para o outro pela hélice de refrigeração.</p>
 
<p>Depois de algumas horas chegamos em nosso destino.
     Conheci pessoas incríveis, que me deram abrigo e também estavam ajudando com os preparativos do evento.
      Familiares da noiva, amigos da noiva, todos me receberam de braços abertos. Vou destacar aqui a mãe da noiva, 
      que está no seleto grupo de senhoras que recebem um cantinho especial  no meu coração, as que me chamam carinhosamente de “fío”,
       que me adotaram, e me dedicaram seu infinito amor materno. </p>

<p>Terminado o descanso, tive um dia agitado carregando alimentos, ajudando com os enfeites,
     esticando uma lona em um quiosque no jardim, pendurando um fio cheio de lâmpadas por cima da piscina da chácara.
      E ajudando a carregar uma mesa mágica, mesa de vidro e peso de chumbo.</p>

<p>O sol quente estava rosando a minha pele clara, e em vários momentos alguém perguntava com um tom de  afirmação:
    "Lá em Santa Catarina não é quente assim né?(!)”. E eu não sabia direito o que responder,
     pois eu mesmo não sentia diferença nenhuma. Mas explicava que em minha cidade o sol também  é cruel quando mal humorado, 
     dependendo das fases da lua. Porém, quando o calor ataca, podemos nos abrigar nas diversas cachoeiras da região.</p>

<p>Lembro da varanda com cadeiras confortáveis e vento fresco, o pé de manga no quintal. 
    O café moído na hora, no moedor ao lado da porta, a praça do trilho de trem e a barraca de caldo de cana.
     Também não esqueço do chopp, ah, aquele chopp! Me dá água na boca só de lembrar.
      E o de uva no cinema do Shopping foi outro inesquecível. </p>

<p>Ao fim da aventura voltei de carona com a prima da noiva até Curitiba, depois peguei o ônibus para a minha cidade.
     O fusca amarelo voltou pelo mesmo caminho uns dias depois, trazendo os noivos para uma nova vida, a vida de casados.</p>

