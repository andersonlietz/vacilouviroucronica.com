                      <h2>Inimigo Anônimo</h2>
                      
	
	<img src="imagens/recado.jpg" class="imagem">Arquivo Pessoal</img>
<p>.</p>
<p>.</p>
         <p>“Bom dia Alexander! 
<p>
  Talvez tenha ficado surpreso com esta singela surpresa.
   </p>
<p>
  Mas não se preocupe, é apenas um aviso,  cuidado com as costas!
</p>
<p>
  Atenciosamente, seu inimigo anônimo.”
</p>
<p>
  Alexander realmente se surpreendeu ao achar o bilhete.
      Estava saindo tomar seu café da manhã quando se deparou com o papel 
      espetado por um punhal na lateral da porta.
    </p>
<p>
  Leu o conteúdo e ficou se perguntando quem seria o tal “Inimigo Anônimo”.
   Não tinha conhecimento de ninguém que lhe desejasse o mal, 
   principalmente por aqueles dias de tranquilidade.
Seria algum colega de infância ainda guardando mágoa de uma brincadeira irresponsável?
Algum comprador ou vendedor insatisfeito com o negócio?
 Alguma ex namorada ainda rancorosa com a separação?
  Talvez algum dos ex sogros malucos que teve? 
 Alguma mulher o acusou falsamente ao pai como sendo o motivo de estar grávida? 
 Não, não conseguia compreender,
  sempre usufruiu do respeito dos mais velhos,
   a admiração dos companheiros e o gracejo das damas. 
</p>

<p>
  Fechou a porta, dobrou o bilhete e o colocou no bolso da camisa, 
   acomodou o punhal no bolso interno do casaco.
 Precisava descobrir que arma exótica era aquela,
  e o ferreiro foi o homem que passou primeiro em sua cabeça,
  era o homem que trabalhava com toda sorte de aço,
   provavelmente saberia a procedência da lâmina.
Desceu as escadas e caminhou em direção à ferraria.
 Uma chuva fina e serena  caía aumentando o tamanho das poças de água na estrada de terra.
 
Conforme ia se aproximando da oficina, 
o som  da marreta moldando o  ferro na bigorna ia se intensificando.
</p>
<p>
A porta estava aberta e Alexander entrou. 
Passou pelas estantes de ferragens até ver as faíscas de ferro iluminando um canto.
 Lá estava o ferreiro dando forma a uma foice,
  empenhado profundamente na tarefa, sem perceber a presença de Alexander. 
 Até que a esposa saiu da casa ao lado e colocou a mão nas costas do marido. 
 Este sentiu o toque e parou de martelar, 
 virou-se e viu Alexander escorado em uma parede esperando o profissional dar um tempo.
</p> 
<p>
  — Ele quer falar com você — disse a mulher ao marido
   — O barulho do ferro está deixando ele surdo — lamentou para Alexander.
<p>
  Este  agradeceu o favor da senhora, que caminhou novamente para seus afazeres.
</p>
<p>
— Ora  ora Alexander! O que posso fazer por você? —
 Disse o ferreiro de forma alegre — mais cavalos para mim ferrar?
</p>
<p>
  — Não dessa vez. Têm um tempo pra tomar um café comigo? Eu pago.
</p>
<p>
  — Parece algo importante mesmo! 
  Depois dessa peça aqui eu posso dar uma pausa, mas preciso terminar ela, 
  vai demorar uns minutos ainda — 
  disse o ferreiro colocando a peça novamente na  forja e atiçando as brasas com o fole.
</p>
<p> — Pode ser, eu já vou indo lá na Dona Elsa pra fazer o pedido — 
  disse Alexander saindo da oficina. 
  Atravessou a estrada de terra enlameada desviando de algumas carroças e
   entrou na pequena lanchonete da Dona Elsa,
   viúva empreendedora que possuía a lanchonete e alguns quartos para alugar,
    entre eles o qual Alexander estava hospedado.</p>
<p>— Bom dia Alexander! — ela cumprimentou.</p>
<p>— Bom dia! Como você está? — respondeu Alexander.</p>
<p>—  Tudo ótimo! Amo a época das chuvas! 
   Menos viajantes e turistas pra emporcar a cidade e me assediar,
    tudo fica mais tranquilo.</p>
   <p>Assino embaixo! — disse Alexander com um sorriso —
      eu também aproveito essa época pra tirar umas férias,
      porque fica  difícil de viajar com os cavalos, 
      montar acampamento, fazer fogo e atravessar os rios.</p>
      <p> — Vai querer o mesmo de sempre?
</p>
<p>
 — Isso, gosto de comer bolo quando estou na cidade,
  porque quando viajo só como pão com carne e café. 
 Convidei o mestre da ferraria pra vir tomar café comigo,
  pode servir o mesmo pra ele também.
</p>
<p>
Alexander sentou-se em uma mesa perto da vidraça e viu o
 ferreiro tirar o avental de couro e sair da oficina. 
Em pouco tempo este entrou na lanchonete e sentou-se à mesa.
</p>
<p>
  Elsa trouxe dois pedaços de bolo e duas xícaras com chocolate quente.
   Os dois agradeceram.
</p>
<p>
   — Não quero ser ingrato, mas você me chamou pra tomar café! — 
   falou o ferreiro em tom de brincadeira.
  </p>
<p>
   — Café da manhã, desculpa, foi um erro meu, 
   eu  estou com um pouco de pressa. Mas se quiser eu peço um café.
  </p>
<p> 
  —  Não, sem problema, até é bom tomar essa bebida feminina de vez em quando — 
  disse o ferreiro rindo.
 </p>
<p>
  Alexander ficou um pouco magoado com o preconceito do ferreiro para com o chocolate quente,
   mas relevou. 
  Mordeu o seu pedaço de bolo e enquanto mastigava tirou o punhal do bolso.
</p>
<p>
— Consegue me falar sobre a origem dessa faca? 
Achei ela espetada na minha porta hoje de manhã, com um bilhete não muito amistoso.
</p>
<p>
— Deixe-me ver — disse ele pegando o punhal e o analisando.
</p>
<p>
   — Não é uma simples faca,
    têm até esse veio aqui pra vítima sangrar mesmo se ela não for retirada. 
   Têm um bom equilíbrio entre cabo e lâmina, 
   então pode ser arremessada com facilidade.
    O aço é bom e o estilo me parece que é mais comum no norte. 
    Dificilmente um peão teria uma faca assim,
     mais provável um fazendeiro. 
    As pedras podem não ser preciosas,
     mas prender elas no aço não é pra qualquer ferreiro como eu, 
    exigiu o trabalho de algum ourives. 
    Pegue, não gosto de segurar algo assim, quem sabe quantas vidas ela já tirou
     — disse ele entregando a arma pra Alexander.
    </p>
<p>
— Faz sentido, mas não tenho ideia de alguém com esse perfil que me deseje o mal.
   Talvez algum fazendeiro do norte não ficou satisfeito com os meus cavalos.
  </p>
  <p>
  — Também acredito nisso, 
  não conheço alguém aqui que faria algo dessa natureza com você,
   a maioria te ama.
     Mas quem deu esse recado espera que você amoleça o pulso,
      fique apavorado igual um porco fujão, pra daí te acertar.
  </p>
  <p>
  — Também pensei nisso — 
  disse Alexander esvaziando a xícara — Elsa! — chamou.
</p>
<p>
— Sim! — surgiu ela na porta da cozinha.
</p>
<p>
 — Apareceu algum forasteiro por aqui? 
 Talvez um fazendeiro do norte? — perguntou Alexander.
 </p>
<p>
 — Aqui não, mas sei que  três homens do norte estão hospedados no Beerman.
  Chegaram ontem de manhã e o inspetor está de olho neles. 
</p>
<p> 
— Entendi! Obrigado Elsa!
</p>
<p>
 — O inspetor, porque não falou com o inspetor? — perguntou o ferreiro.
</p>
<p> 
— Eu acredito na competência dele, mas decidi ver o que descobria por conta própria. 
Vou ver se encontro esses forasteiros no Beerman.
</p>
<p>
— Você acha que são eles?
</p>
<p>
 — Agora tenho quase certeza,
  talvez um comprador novo que ainda não conhece a minha reputação, e dois peões.
</p>
<p>
— Vi que você está desarmado, quer o revólver que eu tenho lá na oficina? 
</p>
<p> — Obrigado mas não quero resolver as coisas assim. 
  Só uso as armas quando viajo. 
  Quando um coelho aparece na estrada é mais fácil sacar o revólver do que pegar o rifle.
  E isso  quando os indios não o acertam primeiro.
</p>
<p>— Felizes os pacificadores —
   disse o ferreiro sorrindo ao citar o trecho  bíblico —
    como assim Indios?</p>
<p>— Pra passar em paz pela reserva eu costumo presentear o líder com bons cavalos. 
  Ele passou a mandar dois guerreiros me escoltarem até atravessar a reserva,
   homem de respeito que é. </p>
  <p>— Me permita citar mais um ditado: 
    "Se não pode contra o inimigo, junte-se a ele!" - disse o ferreiro.</p>
  <p> 
    — Marca na minha conta Elsa! —
     disse  Alexander se erguendo da cadeira. 
  </p>
<p>
   — Pode deixar! — respondeu ela da cozinha.

</p>
<p>
 — Acho que eu vou junto pra ver isso.
  Se eu morrer a culpa é tua — disse o ferreiro em tom de brincadeira.
</p>
<p>
— Pode deixar! disse Alexander com um sorriso ao pegar o chapéu da parede  e sair da lanchonete.
</p>
<p>
Ao caminhar um pouco pelo assoalho dos estabelecimentos, logo chegaram ao Beerman.
 O ferreiro entrou na frente e já pediu uma cerveja. 
 Alexander olhou as mesas procurando os forasteiros de quem Dona Elsa havia falado.
  Achou dois deles jogando cartas em uma mesa aos fundos.
  Reconheceu o fazendeiro para o qual havia vendido um cavalo meses antes.
</p>
<p>
— Posso me juntar a vocês? — perguntou.
</p>
<p> 
— Olha quem apareceu! Mas é claro, sente-se!
</p>
<p>
Alexander puxou a cadeira e sentou-se com eles.
</p>
<p> — Talvez seja erro meu, mas acredito que esta faca é sua —
   disse mostrando o punhal.</p>
<p> — Sim, é minha sim, ganhei em uma aposta a uns dias .</p>
<p>Tome novamente, e não deixe perdida  por aí — 
  disse alexander entregando o punhal. — Veio atrás de mim? Porquê?</p>
<p> — Aquele cavalo era coisa de outro mundo,
    forte, lindo, uma pelagem sem igual.
   Foi só você vir embora ele se transformou, 
   emagreceu, adoeceu e tive que o enterrar. 
   Mas eu sei o que aconteceu! Foi maldição de índio,
    eu vi eles com você! — disse o homem aumentando o volume e tom da voz — 
    em vez de denunciar ao inspetor lá do norte, vim atrás de você procurando justiça, seja o que Deus quiser!</p>
<p>
— Falou sobre o meu caráter com os teus vizinhos que também fizeram negócios comigo?
Nunca aconteceu de um dos meus cavalos  morrer tão jovem nas mãos de um comprador.
 — deu uma pausa e depois continuou: 
 — Todo mundo sabe que eu prefiro sacrificar um animal doente do que vendê-lo a alguém.
  Você também conhece a realidade, 
  sabe muito bem que qualquer um de nós está sujeito a morrer a qualquer momento,
   quanto mais um cavalo!
 </p>
<p>
O homem foi sendo impressionado pela sinceridade de Alexander.
</p>
<p>
 — E o teu azar é meu azar, 
 posso te devolver o dinheiro ou arrumar outro tão lindo quanto o outro, 
 há muitos criadores excelentes por essas bandas,
  pode até pegar o dinheiro e negociar direto com um deles se não confia mais em mim.
</p>
<p>
  Tudo bem. Pelo que pude ouvir você têm boa reputação. 
  Acho que posso confiar em você novamente.
</p>
  <p>
     — Bom ouvir isso! Mais um cliente satisfeito — riu.
    </p>
<p> O ferreiro se uniu a eles esquecendo da oficina aberta e
   Alexander pediu uma cerveja. 
Combinaram a nova entrega do cavalo, 
o fazendeiro voltou com seus guarda costas para o norte e Alexander 
continuou aproveitando suas férias.
</p>

